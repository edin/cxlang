        public ContiguousIterableInfo? ResolveContiguousIterable(string expression)
        {
            expression = expression.Trim();
            if (!_variables.TryGetValue(expression, out var type))
            {
                return null;
            }

            var isPointer = type.EndsWith("*", StringComparison.Ordinal);
            if (TryParseFixedArrayType(type, out var arrayElementType, out var arrayLength))
            {
                return new ContiguousIterableInfo(
                    arrayElementType,
                    arrayLength,
                    expression);
            }

            var access = isPointer ? "->" : ".";
            var contiguous = _requirementMatcher.Match(type, "Contiguous");
            if (contiguous.Success && contiguous.TypeBindings.TryGetValue("T", out var valueType))
            {
                return new ContiguousIterableInfo(
                    valueType,
                    $"{expression}{access}length",
                    $"{expression}{access}data");
            }

            var range = _requirementMatcher.Match(type, "ContiguousRange");
            if (range.Success && range.TypeBindings.TryGetValue("T", out var rangeValueType))
            {
                return new ContiguousIterableInfo(
                    rangeValueType,
                    $"({expression}{access}end - {expression}{access}start)",
                    $"{expression}{access}start");
            }

            return null;
        }

        public IteratorIterableInfo? ResolveIteratorIterable(ExpressionNode expression, bool keyValue)
        {
            var source = expression.SourceText.Trim();
            if (!_variables.TryGetValue(source, out var type))
            {
                return null;
            }

            var iterableRequirement = keyValue ? "KeyValueIterable" : "Iterable";
            var iterable = _requirementMatcher.Match(type, iterableRequirement);
            if (!iterable.Success || !iterable.TypeBindings.TryGetValue("I", out var iteratorType))
            {
                return ResolveIteratorIterableFromIteratorMethod(expression, keyValue);
            }

            if (keyValue)
            {
                if (!iterable.TypeBindings.TryGetValue("K", out var keyType)
                    || !iterable.TypeBindings.TryGetValue("V", out var valueType))
                {
                    return null;
                }

                return new IteratorIterableInfo(
                    iteratorType,
                    valueType,
                    keyType,
                    BuildIteratorInitializer(expression));
            }

            if (!iterable.TypeBindings.TryGetValue("T", out var itemType))
            {
                return null;
            }

            return new IteratorIterableInfo(
                iteratorType,
                itemType,
                KeyType: null,
                BuildIteratorInitializer(expression));
        }

        private IteratorIterableInfo? ResolveIteratorIterableFromIteratorMethod(ExpressionNode expression, bool keyValue)
        {
            var source = expression.SourceText.Trim();
            var iteratorCall = new CallExpressionNode(
                expression.Location,
                source + ".iterator()",
                new MemberExpressionNode(
                    expression.Location,
                    source + ".iterator",
                    expression,
                    "iterator"),
                []);
            var iteratorType = ResolveExpressionType(iteratorCall);
            if (iteratorType is null)
            {
                return null;
            }

            var iteratorRequirement = keyValue ? "KeyValueIterator" : "Iterator";
            var iterator = _requirementMatcher.Match(iteratorType, iteratorRequirement);
            if (!iterator.Success)
            {
                return null;
            }

            if (keyValue)
            {
                if (!iterator.TypeBindings.TryGetValue("K", out var keyType)
                    || !iterator.TypeBindings.TryGetValue("V", out var valueType))
                {
                    return null;
                }

                return new IteratorIterableInfo(
                    iteratorType,
                    valueType,
                    keyType,
                    BuildIteratorInitializer(expression));
            }

            if (!iterator.TypeBindings.TryGetValue("T", out var itemType))
            {
                return null;
            }

            return new IteratorIterableInfo(
                iteratorType,
                itemType,
                KeyType: null,
                BuildIteratorInitializer(expression));
        }

        private CExpression BuildIteratorInitializer(ExpressionNode expression)
        {
            var source = expression.SourceText.Trim();
            return LowerExpression(new CallExpressionNode(
                expression.Location,
                source + ".iterator()",
                new MemberExpressionNode(
                    expression.Location,
                    source + ".iterator",
                    expression,
                    "iterator"),
                []));
        }

        public MatchInfo? ResolveMatch(string expression)
        {
            expression = expression.Trim();
            if (!_variables.TryGetValue(expression, out var type))
            {
                return null;
            }

            var normalizedType = NormalizeType(type);
            if (_taggedUnions.TryGetValue(normalizedType, out var taggedUnion))
            {
                var isPointer = type.EndsWith("*", StringComparison.Ordinal);
                return MatchInfo.ForTaggedUnion(
                    normalizedType,
                    taggedUnion,
                    isPointer ? $"{expression}->tag" : $"{expression}.tag",
                    isPointer ? $"{expression}->as" : $"{expression}.as");
            }

            if (_interfaces.TryGetValue(normalizedType, out var interfaceNode))
            {
                var isPointer = type.EndsWith("*", StringComparison.Ordinal);
                var access = isPointer ? "->" : ".";
                var implementations = _interfaceImplementations.Values
                    .Where(implementation => implementation.Interface.Name == interfaceNode.Name)
                    .ToDictionary(implementation => implementation.Struct.Name, StringComparer.Ordinal);
                return MatchInfo.ForInterface(
                    interfaceNode,
                    $"{expression}{access}vtable->type_id",
                    $"{expression}{access}state",
                    implementations);
            }

            return null;
        }

        public string LowerInitializer(string targetType, string expression)
        {
            var lowered = Lower(expression);
            if (TryBuildInterfaceValue(targetType, expression, out var interfaceInitializer))
            {
                return interfaceInitializer;
            }

            return TryWrapTaggedUnionValue(targetType, expression, lowered) ?? lowered;
        }

        public string LowerInitializer(string targetType, ExpressionNode expression)
        {
            var lowered = expression is InitializerExpressionNode initializer
                ? LowerInitializerExpression(initializer, targetType)
                : Lower(expression);
            if (TryBuildInterfaceValue(targetType, expression.SourceText, out var interfaceInitializer))
            {
                return interfaceInitializer;
            }

            return TryWrapTaggedUnionValue(targetType, expression.SourceText, lowered) ?? lowered;
        }

        public CExpression LowerInitializerExpression(string targetType, ExpressionNode expression)
        {
            var direct = expression is InitializerExpressionNode initializer
                ? LowerInitializerExpressionNode(initializer, targetType)
                : LowerExpression(expression);
            if (TryBuildInterfaceValueExpression(targetType, expression.SourceText) is { } interfaceInitializer)
            {
                return interfaceInitializer;
            }

            if (TryWrapTaggedUnionValueExpression(targetType, expression.SourceText, direct) is { } wrapped)
            {
                return wrapped;
            }

            var lowered = LowerInitializer(targetType, expression);
            return string.Equals(lowered, _expressionEmitter.Emit(direct), StringComparison.Ordinal)
                ? direct
                : new CRawExpression(lowered);
        }

        private bool TryBuildInterfaceValue(string targetType, string sourceExpression, out string initializer)
        {
            initializer = string.Empty;
            if (TryBuildInterfaceValueExpression(targetType, sourceExpression) is not { } expression)
            {
                return false;
            }

            initializer = _expressionEmitter.Emit(expression);
            return true;
        }

        private CExpression? TryBuildInterfaceValueExpression(string targetType, string sourceExpression)
        {
            var interfaceName = NormalizeType(targetType);
            if (!_interfaces.ContainsKey(interfaceName))
            {
                return null;
            }

            var trimmedSource = sourceExpression.Trim();
            if (!_variables.TryGetValue(trimmedSource, out var sourceType))
            {
                return null;
            }

            var normalizedSourceType = LowerType(NormalizeType(sourceType), SelfType);
            if (!_interfaceImplementations.ContainsKey((normalizedSourceType, interfaceName)))
            {
                return null;
            }

            var sourceIsPointer = sourceType.TrimEnd().EndsWith("*", StringComparison.Ordinal);
            CExpression state = sourceIsPointer
                ? new CNameExpression(trimmedSource)
                : new CUnaryExpression("&", new CNameExpression(trimmedSource));
            return new CInitializerExpression(
                LowerType(interfaceName, SelfType),
                [
                    new CInitializerField("state", state),
                    new CInitializerField(
                        "vtable",
                        new CUnaryExpression("&", new CNameExpression(GetInterfaceVTableInstanceName(normalizedSourceType, interfaceName)))),
                ],
                []);
        }

        public CExpression LowerExpression(ExpressionNode expression) => expression switch
        {
            LiteralExpressionNode literal => new CLiteralExpression(LowerLiteral(literal.SourceText)),
            NameExpressionNode name => LowerNameExpression(name.SourceText),
            ParenthesizedExpressionNode parenthesized => new CParenthesizedExpression(LowerExpression(parenthesized.Expression)),
            CastExpressionNode cast => new CCastExpression(LowerType(cast.TargetType, SelfType), LowerExpression(cast.Expression)),
            UnaryExpressionNode { Operator: "&" } unary => LowerAddressOfExpression(unary.Operand),
            UnaryExpressionNode { Operator: "*" } unary => new CUnaryExpression(
                unary.Operator,
                LowerExpression(unary.Operand)),
            UnaryExpressionNode unary => ShouldUseRawLowering(unary.SourceText)
                ? new CRawExpression(Lower(unary.SourceText))
                : new CUnaryExpression(unary.Operator, LowerExpression(unary.Operand)),
            PostfixExpressionNode postfix => new CPostfixExpression(LowerExpression(postfix.Operand), postfix.Operator),
            SizeOfExpressionNode sizeOf => LowerSizeOfExpression(sizeOf),
            BinaryExpressionNode binary => LowerBinaryExpression(binary),
            ConditionalExpressionNode conditional => LowerConditionalExpression(conditional),
            ScalarRangeExpressionNode range => new CRawExpression(Lower(range.SourceText)),
            InitializerExpressionNode initializer => LowerInitializerExpressionNode(initializer),
            FunctionExpressionNode functionExpression => new CRawExpression(Lower(functionExpression.SourceText)),
            AssignmentExpressionNode assignment => LowerAssignmentExpression(assignment),
            CallExpressionNode call => LowerCallExpression(call) is { } loweredCall
                ? loweredCall
                : new CRawExpression(Lower(call.SourceText)),
            GenericCallExpressionNode call => LowerGenericCallExpression(call) is { } loweredGenericCall
                ? loweredGenericCall
                : new CRawExpression(Lower(call.SourceText)),
            MemberExpressionNode member => ShouldUseRawLowering(member.SourceText)
                ? new CRawExpression(Lower(member.SourceText))
                : LowerMemberExpression(member),
            IndexExpressionNode index => ShouldUseRawLowering(index.SourceText)
                ? new CRawExpression(Lower(index.SourceText))
                : new CIndexExpression(LowerExpression(index.Target), LowerExpression(index.Index)),
            RawExpressionNode raw => TryLowerRawExpression(raw.SourceText) ?? new CRawExpression(Lower(raw.SourceText)),
            _ => new CRawExpression(Lower(expression.SourceText)),
        };

        public string Lower(ExpressionNode expression) => expression switch
        {
            LiteralExpressionNode literal => LowerLiteral(literal.SourceText),
            NameExpressionNode name => LowerNameForValue(name.SourceText),
            ParenthesizedExpressionNode parenthesized => $"({Lower(parenthesized.Expression)})",
            CastExpressionNode cast => $"({LowerType(cast.TargetType, SelfType)}) {Lower(cast.Expression)}",
            UnaryExpressionNode { Operator: "&" } unary => LowerAddressOf(unary.Operand),
            UnaryExpressionNode unary => ShouldUseRawLowering(unary.SourceText)
                ? Lower(unary.SourceText)
                : unary.Operator + Lower(unary.Operand),
            PostfixExpressionNode postfix => $"{Lower(postfix.Operand)}{postfix.Operator}",
            SizeOfExpressionNode sizeOf => LowerSizeOf(sizeOf),
            BinaryExpressionNode binary => LowerBinary(binary),
            ConditionalExpressionNode conditional => LowerConditional(conditional),
            ScalarRangeExpressionNode range => Lower(range.SourceText),
            InitializerExpressionNode initializer => LowerInitializerExpression(initializer),
            FunctionExpressionNode functionExpression => Lower(functionExpression.SourceText),
            AssignmentExpressionNode assignment => LowerAssignment(assignment),
            CallExpressionNode call => LowerCall(call) ?? Lower(call.SourceText),
            GenericCallExpressionNode call => LowerGenericCall(call) ?? Lower(call.SourceText),
            MemberExpressionNode member => ShouldUseRawLowering(member.SourceText)
                ? Lower(member.SourceText)
                : LowerMember(member),
            IndexExpressionNode index => ShouldUseRawLowering(index.SourceText)
                ? Lower(index.SourceText)
                : $"{Lower(index.Target)}[{Lower(index.Index)}]",
            RawExpressionNode raw => Lower(raw.SourceText),
            _ => Lower(expression.SourceText),
        };

        private CExpression LowerBinaryExpression(BinaryExpressionNode binary)
        {
            if (ShouldUseRawLowering(binary.SourceText))
            {
                return new CRawExpression(Lower(binary.SourceText));
            }

            return binary.Operator == "<=>"
                ? new CCallExpression(new CFunctionName("compare"), [LowerExpression(binary.Left), LowerExpression(binary.Right)])
                : new CBinaryExpression(LowerExpression(binary.Left), binary.Operator, LowerExpression(binary.Right));
        }

        private CExpression LowerSizeOfExpression(SizeOfExpressionNode sizeOf)
        {
            if (!string.IsNullOrWhiteSpace(sizeOf.TypeOperand))
            {
                return new CSizeOfTypeExpression(LowerType(sizeOf.TypeOperand, SelfType));
            }

            return sizeOf.ExpressionOperand is null
                ? new CSizeOfTypeExpression("void")
                : new CSizeOfExpression(LowerExpression(sizeOf.ExpressionOperand));
        }

        private CExpression LowerConditionalExpression(ConditionalExpressionNode conditional)
        {
            if (ShouldUseRawLowering(conditional.SourceText))
            {
                return new CRawExpression(Lower(conditional.SourceText));
            }

            return new CConditionalExpression(
                LowerExpression(conditional.Condition),
                LowerExpression(conditional.WhenTrue),
                LowerExpression(conditional.WhenFalse));
        }

        private CExpression LowerInitializerExpressionNode(InitializerExpressionNode initializer, string? targetType = null) =>
            new CInitializerExpression(
                initializer.TypeName is null ? null : LowerType(initializer.TypeName, SelfType),
                initializer.Fields
                    .Select(field => new CInitializerField(field.Name, LowerExpression(field.Value)))
                    .ToList(),
                initializer.Values.Select(LowerExpression).ToList());

        private CExpression? TryLowerRawExpression(string expression)
        {
            expression = expression.Trim();
            var dereferenceCall = Regex.Match(expression, @"^\*\s*(?<name>[A-Za-z_][A-Za-z0-9_]*)\s*\(");
            if (!dereferenceCall.Success)
            {
                return null;
            }

            var functionName = dereferenceCall.Groups["name"].Value;
            var openParen = expression.IndexOf('(', dereferenceCall.Groups["name"].Index + functionName.Length);
            var closeParen = FindMatchingParen(expression, openParen);
            if (openParen < 0 || closeParen != expression.Length - 1)
            {
                return null;
            }

            var argumentsText = expression[(openParen + 1)..closeParen];
            var arguments = SplitArguments(argumentsText)
                .Select(LowerSimpleRawArgument)
                .ToList();
            return new CUnaryExpression(
                "*",
                new CCallExpression(new CFunctionName(functionName), arguments));
        }

        private CExpression LowerSimpleRawArgument(string argument)
        {
            argument = argument.Trim();
            if (argument.StartsWith("&", StringComparison.Ordinal))
            {
                return new CUnaryExpression("&", ToCSimpleAccessExpression(argument[1..]));
            }

            if (Regex.IsMatch(argument, @"^\d+(?:\.\d+)?$"))
            {
                return new CLiteralExpression(argument);
            }

            return ToCSimpleAccessExpression(Lower(argument));
        }

        private string LowerLiteral(string text) => text switch
        {
            "true" => "1",
            "false" => "0",
            "null" => "NULL",
            _ => text,
        };

        private string LowerName(string name)
        {
            if (_symbolAliases.TryGetValue(name, out var original))
            {
                return original;
            }

            return name;
        }

        private CExpression LowerNameExpression(string name)
        {
            var loweredName = LowerName(name);
            return _implicitReferenceLocals.ContainsKey(name)
                ? new CUnaryExpression("*", new CNameExpression(loweredName))
                : new CNameExpression(loweredName);
        }

        private string LowerNameForValue(string name)
        {
            var loweredName = LowerName(name);
            return _implicitReferenceLocals.ContainsKey(name)
                ? "*" + loweredName
                : loweredName;
        }

        private CExpression LowerAddressOfExpression(ExpressionNode operand)
        {
            if (operand is NameExpressionNode name
                && _implicitReferenceLocals.ContainsKey(name.SourceText))
            {
                return new CNameExpression(LowerName(name.SourceText));
            }

            return new CUnaryExpression("&", LowerExpression(operand));
        }

        private string LowerAddressOf(ExpressionNode operand)
        {
            if (operand is NameExpressionNode name
                && _implicitReferenceLocals.ContainsKey(name.SourceText))
            {
                return LowerName(name.SourceText);
            }

            return "&" + Lower(operand);
        }

        private string? LowerCall(CallExpressionNode call)
        {
            if (call.Callee is MemberExpressionNode member)
            {
                return LowerMemberCall(member, call.Arguments);
            }

            if (call.Callee is NameExpressionNode name)
            {
                if (_structs.TryGetValue(name.SourceText, out var structNode))
                {
                    return LowerStructConstructor(structNode, call.Arguments.Select(argument => argument.SourceText).ToList());
                }

                if (_taggedUnions.ContainsKey(name.SourceText))
                {
                    return null;
                }

                var genericCall = FindInferredGenericCall(null, name.SourceText, call.Arguments, skipSelf: false);
                if (genericCall is not null)
                {
                    return EmitCallExpression(
                        new CResolvedFunction(ModuleName: GetFunctionModule(genericCall.OwnerType, genericCall.Name), genericCall.CName),
                        call.Arguments);
                }

                return EmitCallExpression(new CFunctionName(LowerName(name.SourceText)), call.Arguments);
            }

            return null;
        }

        private string LowerBinary(BinaryExpressionNode binary)
        {
            if (ShouldUseRawLowering(binary.SourceText))
            {
                return Lower(binary.SourceText);
            }

            if (binary.Operator == "<=>")
            {
                return $"compare({Lower(binary.Left)}, {Lower(binary.Right)})";
            }

            return $"{Lower(binary.Left)} {binary.Operator} {Lower(binary.Right)}";
        }

        private string LowerSizeOf(SizeOfExpressionNode sizeOf)
        {
            if (!string.IsNullOrWhiteSpace(sizeOf.TypeOperand))
            {
                return $"sizeof({LowerType(sizeOf.TypeOperand, SelfType)})";
            }

            return sizeOf.ExpressionOperand is null
                ? "sizeof(void)"
                : $"sizeof({Lower(sizeOf.ExpressionOperand)})";
        }

        private string LowerConditional(ConditionalExpressionNode conditional)
        {
            if (ShouldUseRawLowering(conditional.SourceText))
            {
                return Lower(conditional.SourceText);
            }

            return $"{Lower(conditional.Condition)} ? {Lower(conditional.WhenTrue)} : {Lower(conditional.WhenFalse)}";
        }

        private string LowerInitializerExpression(InitializerExpressionNode initializer, string? targetType = null)
        {
            var fields = initializer.Fields.Select(field => $".{field.Name} = {Lower(field.Value)}");
            var values = initializer.Values.Select(Lower);
            var body = string.Join(", ", fields.Concat(values));
            if (string.IsNullOrWhiteSpace(body))
            {
                body = "0";
            }

            return initializer.TypeName is null
                ? "{ " + body + " }"
                : $"({LowerType(initializer.TypeName, SelfType)}){{ {body} }}";
        }

        private string? LowerGenericCall(GenericCallExpressionNode call)
        {
            var expression = LowerGenericCallExpression(call);
            return expression is null ? null : _expressionEmitter.Emit(expression);
        }

        private CExpression? LowerGenericCallExpression(GenericCallExpressionNode call)
        {
            if (call.Callee is MemberExpressionNode member)
            {
                var memberCall = LowerGenericMemberCallExpression(member, call.TypeArguments, call.Arguments);
                if (memberCall is not null)
                {
                    return memberCall;
                }
            }

            var calleeName = GetQualifiedName(call.Callee);
            if (calleeName is null)
            {
                return null;
            }

            if (_genericMacroNames.Contains(calleeName))
            {
                return new CRawExpression($"{LowerName(calleeName)}({string.Join(", ", call.Arguments.Select(Lower))})");
            }

            var match = _genericCalls.FirstOrDefault(candidate =>
                candidate.OwnerType is null
                && candidate.Name == calleeName
                && SameTypeArguments(candidate.TypeArguments, call.TypeArguments));
            if (match is not null)
            {
                return new CCallExpression(
                    new CResolvedFunction(ModuleName: GetFunctionModule(match.OwnerType, match.Name), match.CName),
                    call.Arguments.Select(LowerExpression).ToList());
            }

            var staticMatch = _genericCalls.FirstOrDefault(candidate =>
                candidate.IsStatic
                && candidate.OwnerType is not null
                && calleeName == $"{candidate.OwnerType}.{candidate.Name}"
                && SameTypeArguments(candidate.TypeArguments, call.TypeArguments));
            if (staticMatch is null
                && TrySplitQualifiedMember(calleeName, out var ownerName, out var memberName)
                && _adapterExposes.TryGetValue($"{ownerName}.{memberName}", out var staticExpose)
                && staticExpose.IsStatic)
            {
                var resolvedExpose = ResolveAdapterExpose(staticExpose, call.TypeArguments);
                staticMatch = _genericCalls.FirstOrDefault(candidate =>
                    candidate.IsStatic
                    && candidate.OwnerType == resolvedExpose.BaseOwner
                    && candidate.Name == resolvedExpose.SourceName
                    && SameTypeArguments(candidate.TypeArguments, resolvedExpose.TypeArguments));
            }

            return staticMatch is null
                ? null
                : new CCallExpression(
                    new CResolvedFunction(ModuleName: GetFunctionModule(staticMatch.OwnerType, staticMatch.Name), staticMatch.CName),
                    call.Arguments.Select(LowerExpression).ToList());
        }

        private string? LowerGenericMemberCall(
            MemberExpressionNode member,
            IReadOnlyList<string> typeArguments,
            IReadOnlyList<ExpressionNode> arguments)
        {
            var expression = LowerGenericMemberCallExpression(member, typeArguments, arguments);
            return expression is null ? null : _expressionEmitter.Emit(expression);
        }

        private CExpression? LowerGenericMemberCallExpression(
            MemberExpressionNode member,
            IReadOnlyList<string> typeArguments,
            IReadOnlyList<ExpressionNode> arguments)
        {
            if (member.Target is not NameExpressionNode targetName
                || !_variables.TryGetValue(targetName.SourceText, out var targetType))
            {
                return null;
            }

            var target = targetName.SourceText;
            foreach (var genericCall in _genericCalls.Where(call =>
                !call.IsStatic
                && call.OwnerType == GetGenericBaseName(targetType)
                && call.Name == member.MemberName
                && SameTypeArguments(call.TypeArguments, typeArguments)))
            {
                var receiver = genericCall.TakesPointerSelf || targetType.EndsWith("*", StringComparison.Ordinal)
                    ? targetType.EndsWith("*", StringComparison.Ordinal) ? target : "&" + target
                    : target;
                var loweredArguments = arguments.Select(LowerExpression).ToList();
                loweredArguments.Insert(0, ReceiverExpression(target, targetType.EndsWith("*", StringComparison.Ordinal), genericCall.TakesPointerSelf));
                return new CCallExpression(
                    new CResolvedFunction(ModuleName: GetFunctionModule(genericCall.OwnerType, genericCall.Name), genericCall.CName),
                    loweredArguments);
            }

            return null;
        }

        private GenericCallInfo? FindInferredGenericCall(
            string? ownerType,
            string name,
            IReadOnlyList<ExpressionNode> arguments,
            bool skipSelf,
            IReadOnlyList<string>? preferredTypeArguments = null)
        {
            var candidates = _genericCalls.Where(call =>
                string.Equals(call.OwnerType, ownerType, StringComparison.Ordinal)
                && call.Name == name);
            if (preferredTypeArguments is { Count: > 0 })
            {
                candidates = candidates
                    .OrderByDescending(call => SameTypeArguments(call.TypeArguments, preferredTypeArguments));
            }

            foreach (var candidate in candidates)
            {
                var parameterTypes = candidate.ParameterTypes
                    .Skip(skipSelf ? 1 : 0)
                    .ToList();
                if (parameterTypes.Count != arguments.Count)
                {
                    continue;
                }

                if (preferredTypeArguments is { Count: > 0 }
                    && SameTypeArguments(candidate.TypeArguments, preferredTypeArguments))
                {
                    return candidate;
                }

                var matches = true;
                for (var i = 0; i < arguments.Count; i++)
                {
                    var argumentType = ResolveExpressionType(arguments[i]);
                    if (argumentType is null || !CanAssign(parameterTypes[i], argumentType))
                    {
                        matches = false;
                        break;
                    }
                }

                if (matches)
                {
                    return candidate;
                }
            }

            return null;
        }

        private string? ResolveExpressionType(ExpressionNode expression) => expression switch
        {
            LiteralExpressionNode literal => ResolveLiteralType(literal.SourceText),
            NameExpressionNode name => _variables.GetValueOrDefault(name.SourceText),
            ParenthesizedExpressionNode parenthesized => ResolveExpressionType(parenthesized.Expression),
            CastExpressionNode cast => cast.TargetType,
            UnaryExpressionNode { Operator: "&" } unary when ResolveExpressionType(unary.Operand) is { } operandType => operandType + "*",
            UnaryExpressionNode { Operator: "*" } unary when ResolveExpressionType(unary.Operand) is { } operandType => UnwrapPointer(operandType),
            UnaryExpressionNode unary => ResolveExpressionType(unary.Operand),
            BinaryExpressionNode binary => ResolveBinaryType(binary),
            ScalarRangeExpressionNode range => ResolveExpressionType(range.Start) ?? ResolveExpressionType(range.End),
            ConditionalExpressionNode conditional => ResolveExpressionType(conditional.WhenTrue) ?? ResolveExpressionType(conditional.WhenFalse),
            CallExpressionNode call => ResolveCallType(call),
            GenericCallExpressionNode call => ResolveGenericCallType(call),
            MemberExpressionNode member => ResolveMemberType(member),
            IndexExpressionNode index => ResolveIndexType(index),
            _ => null,
        };

        public string? ResolveExpressionTypeForLowering(ExpressionNode expression) =>
            ResolveExpressionType(expression);

        private string? ResolveCallType(CallExpressionNode call)
        {
            if (call.Callee is NameExpressionNode name)
            {
                return FindInferredGenericCall(null, name.SourceText, call.Arguments, skipSelf: false)?.ReturnType;
            }

            if (call.Callee is MemberExpressionNode member)
            {
                return ResolveMemberCallType(member, call.Arguments);
            }

            return null;
        }

        private string? ResolveGenericCallType(GenericCallExpressionNode call)
        {
            var calleeName = GetQualifiedName(call.Callee);
            if (calleeName is not null)
            {
                var freeCall = _genericCalls.FirstOrDefault(candidate =>
                    candidate.OwnerType is null
                    && candidate.Name == calleeName
                    && SameTypeArguments(candidate.TypeArguments, call.TypeArguments));
                if (freeCall is not null)
                {
                    return freeCall.ReturnType;
                }

                var staticCall = _genericCalls.FirstOrDefault(candidate =>
                    candidate.IsStatic
                    && candidate.OwnerType is not null
                    && calleeName == $"{candidate.OwnerType}.{candidate.Name}"
                    && SameTypeArguments(candidate.TypeArguments, call.TypeArguments));
                if (staticCall is not null)
                {
                    return staticCall.ReturnType;
                }
            }

            if (call.Callee is MemberExpressionNode member)
            {
                var targetType = ResolveExpressionType(member.Target);
                var owner = targetType is null ? GetQualifiedName(member.Target) : GetGenericBaseName(RemovePointer(NormalizeType(targetType)));
                var match = _genericCalls.FirstOrDefault(candidate =>
                    candidate.OwnerType == owner
                    && candidate.Name == member.MemberName
                    && SameTypeArguments(candidate.TypeArguments, call.TypeArguments));
                return match?.ReturnType;
            }

            return null;
        }

        private string? ResolveMemberCallType(MemberExpressionNode member, IReadOnlyList<ExpressionNode> arguments)
        {
            if (member.Target is not NameExpressionNode targetName
                || !_variables.TryGetValue(targetName.SourceText, out var targetType))
            {
                return null;
            }

            var owner = GetGenericBaseName(targetType);
            return FindInferredGenericCall(owner, member.MemberName, arguments, skipSelf: true)?.ReturnType;
        }

        private string? ResolveMemberType(MemberExpressionNode member)
        {
            var targetType = ResolveExpressionType(member.Target);
            if (targetType is null)
            {
                return null;
            }

            var normalizedType = RemovePointer(NormalizeType(targetType));
            if (_structs.TryGetValue(normalizedType, out var structNode)
                || _structs.TryGetValue(LowerType(normalizedType, SelfType), out structNode))
            {
                return structNode.Fields.FirstOrDefault(field => field.Name == member.MemberName)?.Type;
            }

            return null;
        }

        private string? ResolveIndexType(IndexExpressionNode index)
        {
            var targetType = ResolveExpressionType(index.Target);
            if (targetType is null)
            {
                return null;
            }

            if (TryParseFixedArrayType(targetType, out var elementType, out _))
            {
                return elementType;
            }

            return UnwrapPointer(targetType);
        }

        private string? ResolveBinaryType(BinaryExpressionNode binary)
        {
            if (binary.Operator is "==" or "!=" or "<" or "<=" or ">" or ">=" or "&&" or "||")
            {
                return "bool";
            }

            if (binary.Operator == "<=>")
            {
                return "int";
            }

            return ResolveExpressionType(binary.Left) ?? ResolveExpressionType(binary.Right);
        }

        private static string? ResolveLiteralType(string text)
        {
            text = text.Trim();
            if (text is "true" or "false")
            {
                return "bool";
            }

            if (text.StartsWith("\"", StringComparison.Ordinal))
            {
                return "char*";
            }

            if (text.StartsWith("'", StringComparison.Ordinal))
            {
                return "char";
            }

            if (Regex.IsMatch(text, @"^-?\d+$"))
            {
                return "int";
            }

            if (Regex.IsMatch(text, @"^-?(\d+\.\d*|\d*\.\d+)([eE][+-]?\d+)?$"))
            {
                return "double";
            }

            return null;
        }

        private static bool CanAssign(string targetType, string sourceType) =>
            string.Equals(targetType.Trim(), sourceType.Trim(), StringComparison.Ordinal)
            || (targetType.Trim() == "double" && sourceType.Trim() is "int" or "float")
            || (targetType.Trim() == "float" && sourceType.Trim() == "int")
            || (IsNumericType(targetType) && IsNumericType(sourceType));

        private static bool IsNumericType(string type)
        {
            type = type.Trim();
            return type is
                "char" or
                "signed char" or
                "unsigned char" or
                "short" or
                "unsigned short" or
                "int" or
                "unsigned int" or
                "long" or
                "unsigned long" or
                "long long" or
                "unsigned long long" or
                "usize" or
                "i8" or
                "i16" or
                "i32" or
                "i64" or
                "u8" or
                "u16" or
                "u32" or
                "u64" or
                "float" or
                "double" or
                "long double";
        }

        private static string? UnwrapPointer(string type)
        {
            type = type.Trim();
            return type.EndsWith("*", StringComparison.Ordinal)
                ? type[..^1].TrimEnd()
                : null;
        }

        private static string RemovePointer(string type)
        {
            while (type.TrimEnd().EndsWith("*", StringComparison.Ordinal))
            {
                type = type.TrimEnd()[..^1];
            }

            return type.TrimEnd();
        }

        private static bool SameTypeArguments(
            IReadOnlyList<string> left,
            IReadOnlyList<string> right) =>
            left.Count == right.Count
            && left.Zip(right).All(pair => string.Equals(pair.First, pair.Second, StringComparison.Ordinal));

        private static string? GetQualifiedName(ExpressionNode expression) => expression switch
        {
            NameExpressionNode name => name.SourceText,
            MemberExpressionNode member when GetQualifiedName(member.Target) is { } target => $"{target}.{member.MemberName}",
            _ => null,
        };

        private string LowerAssignment(AssignmentExpressionNode assignment)
        {
            if (ShouldUseRawLowering(assignment.SourceText, allowAssignment: true))
            {
                return Lower(assignment.SourceText);
            }

            var loweredTarget = Lower(assignment.Target);
            var loweredValue = Lower(assignment.Value);
            if (assignment.Operator == "="
                && assignment.Target is NameExpressionNode targetName
                && _variables.TryGetValue(targetName.SourceText, out var targetType)
                && TryWrapTaggedUnionValue(targetType, assignment.Value.SourceText, loweredValue) is { } wrappedValue)
            {
                loweredValue = wrappedValue;
            }

            return $"{loweredTarget} {assignment.Operator} {loweredValue}";
        }

        private CExpression LowerAssignmentExpression(AssignmentExpressionNode assignment)
        {
            if (ShouldUseRawLowering(assignment.SourceText, allowAssignment: true))
            {
                return new CRawExpression(Lower(assignment.SourceText));
            }

            var value = LowerExpression(assignment.Value);
            if (assignment.Operator == "="
                && assignment.Target is NameExpressionNode targetName
                && _variables.TryGetValue(targetName.SourceText, out var targetType)
                && TryWrapTaggedUnionValueExpression(targetType, assignment.Value.SourceText, value) is { } wrappedValue)
            {
                value = wrappedValue;
            }

            var target = LowerExpression(assignment.Target);
            if (target is CRawExpression rawTarget
                && TryLowerRawExpression(rawTarget.Text) is { } loweredTarget)
            {
                target = loweredTarget;
            }

            return new CAssignmentExpression(
                target,
                assignment.Operator,
                value);
        }

        private static bool ShouldUseRawLowering(string expression, bool allowAssignment = false) =>
            Regex.IsMatch(expression, @"(?<!<)=>")
            || expression.Contains("->", StringComparison.Ordinal)
            || (!allowAssignment && Regex.IsMatch(expression, @"[\+\-\*/%]\s*="))
            || (!allowAssignment && Regex.IsMatch(expression, @"(?<![=!<>])=(?![=>])"));

        private string LowerMember(MemberExpressionNode member)
        {
            var qualifiedMember = $"{GetQualifiedName(member.Target)}.{member.MemberName}";
            if (_enumMemberAliases.TryGetValue(qualifiedMember, out var enumMemberName))
            {
                return enumMemberName;
            }

            var staticMethodKey = $"{GetQualifiedName(member.Target)}.{member.MemberName}";
            if (_methodNames.TryGetValue(staticMethodKey, out var staticCName))
            {
                return staticCName;
            }

            if (GetQualifiedName(member.Target) is { } moduleTarget && IsModuleQualifierTarget(moduleTarget))
            {
                return member.MemberName;
            }

            if (member.Target is NameExpressionNode targetName
                && _variables.TryGetValue(targetName.SourceText, out var targetType))
            {
                var targetIsImplicitReference = _implicitReferenceLocals.ContainsKey(targetName.SourceText);
                var normalizedType = NormalizeType(targetType);
                if (_taggedUnions.TryGetValue(normalizedType, out var taggedUnion)
                    && taggedUnion.Variants.Any(variant => variant.Name == member.MemberName))
                {
                    if (taggedUnion.IsRaw)
                    {
                        var rawAccess = targetIsImplicitReference || targetType.EndsWith("*", StringComparison.Ordinal)
                            ? "->"
                            : ".";
                        return targetName.SourceText + rawAccess + member.MemberName;
                    }

                    var access = targetIsImplicitReference || targetType.EndsWith("*", StringComparison.Ordinal)
                        ? "->as."
                        : ".as.";
                    return targetName.SourceText + access + member.MemberName;
                }

                if (targetIsImplicitReference || targetType.EndsWith("*", StringComparison.Ordinal))
                {
                    return targetName.SourceText + "->" + member.MemberName;
                }
            }

            return $"{Lower(member.Target)}.{member.MemberName}";
        }

        private CExpression LowerMemberExpression(MemberExpressionNode member)
        {
            var qualifiedMember = $"{GetQualifiedName(member.Target)}.{member.MemberName}";
            if (_enumMemberAliases.TryGetValue(qualifiedMember, out var enumMemberName))
            {
                return new CNameExpression(enumMemberName);
            }

            var staticMethodKey = $"{GetQualifiedName(member.Target)}.{member.MemberName}";
            if (_methodNames.TryGetValue(staticMethodKey, out var staticCName))
            {
                return new CNameExpression(staticCName);
            }

            if (GetQualifiedName(member.Target) is { } moduleTarget && IsModuleQualifierTarget(moduleTarget))
            {
                return new CNameExpression(member.MemberName);
            }

            if (member.Target is NameExpressionNode targetName
                && _variables.TryGetValue(targetName.SourceText, out var targetType))
            {
                var targetIsImplicitReference = _implicitReferenceLocals.ContainsKey(targetName.SourceText);
                var normalizedType = NormalizeType(targetType);
                if (_taggedUnions.TryGetValue(normalizedType, out var taggedUnion)
                    && taggedUnion.Variants.Any(variant => variant.Name == member.MemberName))
                {
                    var access = taggedUnion.IsRaw
                        ? targetIsImplicitReference || targetType.EndsWith("*", StringComparison.Ordinal) ? "->" : "."
                        : targetIsImplicitReference || targetType.EndsWith("*", StringComparison.Ordinal) ? "->as." : ".as.";
                    return new CMemberExpression(new CNameExpression(targetName.SourceText), access, member.MemberName);
                }

                if (targetIsImplicitReference || targetType.EndsWith("*", StringComparison.Ordinal))
                {
                    return new CMemberExpression(new CNameExpression(targetName.SourceText), "->", member.MemberName);
                }
            }

            return new CMemberExpression(LowerExpression(member.Target), ".", member.MemberName);
        }

        private CExpression? LowerCallExpression(CallExpressionNode call)
        {
            if (call.Callee is MemberExpressionNode member)
            {
                if (TryLowerTaggedUnionConstructorExpression(member, call.Arguments) is { } taggedUnionConstructor)
                {
                    return taggedUnionConstructor;
                }

                return LowerMemberCallExpression(member, call.Arguments);
            }

            if (call.Callee is NameExpressionNode name)
            {
                if (_structs.TryGetValue(name.SourceText, out var structNode))
                {
                    return LowerStructConstructorExpression(structNode, call.Arguments);
                }

                if (_taggedUnions.ContainsKey(name.SourceText))
                {
                    return null;
                }

                var genericCall = FindInferredGenericCall(null, name.SourceText, call.Arguments, skipSelf: false);
                if (genericCall is not null)
                {
                    return new CCallExpression(
                        new CResolvedFunction(ModuleName: GetFunctionModule(genericCall.OwnerType, genericCall.Name), genericCall.CName),
                        call.Arguments.Select(LowerExpression).ToList());
                }

                return new CCallExpression(
                    new CFunctionName(LowerName(name.SourceText)),
                    call.Arguments.Select(LowerExpression).ToList());
            }

            return null;
        }

        private CExpression? TryLowerTaggedUnionConstructorExpression(
            MemberExpressionNode member,
            IReadOnlyList<ExpressionNode> arguments)
        {
            if (GetQualifiedName(member.Target) is not { } targetName
                || !_taggedUnions.TryGetValue(targetName, out var taggedUnion)
                || taggedUnion.IsRaw)
            {
                return null;
            }

            var variant = taggedUnion.Variants.FirstOrDefault(variant => variant.Name == member.MemberName);
            if (variant is null)
            {
                return null;
            }

            return new CInitializerExpression(
                LowerType(taggedUnion.Name, SelfType),
                [
                    new CInitializerField("tag", new CNameExpression($"{taggedUnion.Name}_Tag_{variant.Name}")),
                    new CInitializerField("as." + variant.Name, LowerPayloadConstructorExpression(variant.Type, arguments)),
                ],
                []);
        }

        private CExpression LowerPayloadConstructorExpression(
            string payloadType,
            IReadOnlyList<ExpressionNode> arguments)
        {
            var normalizedPayloadType = NormalizeType(payloadType);
            if (_structs.TryGetValue(normalizedPayloadType, out var structNode))
            {
                if (arguments.Count == 1
                    && InferExpressionType(arguments[0].SourceText) == normalizedPayloadType)
                {
                    return LowerExpression(arguments[0]);
                }

                if (arguments.Count == structNode.Fields.Count)
                {
                    return new CInitializerExpression(
                        LowerType(structNode.Name, SelfType),
                        structNode.Fields
                            .Zip(arguments, (field, argument) => new CInitializerField(field.Name, LowerExpression(argument)))
                            .ToList(),
                        []);
                }
            }

            return arguments.Count == 1
                ? LowerExpression(arguments[0])
                : new CRawExpression(LowerPayloadConstructor(payloadType, arguments.Select(argument => argument.SourceText).ToList()));
        }

        private CExpression LowerStructConstructorExpression(
            StructNode structNode,
            IReadOnlyList<ExpressionNode> arguments)
        {
            if (arguments.Count != structNode.Fields.Count)
            {
                return new CRawExpression(LowerStructConstructor(structNode, arguments.Select(argument => argument.SourceText).ToList()));
            }

            return new CInitializerExpression(
                LowerType(structNode.Name, SelfType),
                structNode.Fields
                    .Zip(arguments, (field, argument) => new CInitializerField(field.Name, LowerExpression(argument)))
                    .ToList(),
                []);
        }

        private string? LowerMemberCall(
            MemberExpressionNode member,
            IReadOnlyList<ExpressionNode> arguments)
        {
            var expression = LowerMemberCallExpression(member, arguments);
            return expression is null ? null : _expressionEmitter.Emit(expression);
        }

        private CExpression? LowerMemberCallExpression(
            MemberExpressionNode member,
            IReadOnlyList<ExpressionNode> arguments)
        {
            if (TryLowerInterfaceMemberCallExpression(member, arguments) is { } interfaceCall)
            {
                return interfaceCall;
            }

            if (TryLowerKnownMemberCallExpression(member, arguments) is { } knownMemberCall)
            {
                return knownMemberCall;
            }

            if (member.Target is not NameExpressionNode targetName)
            {
                return null;
            }

            var target = targetName.SourceText;
            if (!_variables.TryGetValue(target, out var targetType))
            {
                if (_adapterExposes.TryGetValue($"{target}.{member.MemberName}", out var staticExpose)
                    && staticExpose.IsStatic)
                {
                    var resolvedExpose = ResolveAdapterExpose(staticExpose, []);
                    var staticGenericExpose = FindInferredGenericCall(
                        resolvedExpose.BaseOwner,
                        resolvedExpose.SourceName,
                        arguments,
                        skipSelf: false,
                        preferredTypeArguments: resolvedExpose.TypeArguments);
                    if (staticGenericExpose is not null)
                    {
                        return new CCallExpression(
                            new CResolvedFunction(ModuleName: GetFunctionModule(staticGenericExpose.OwnerType, staticGenericExpose.Name), staticGenericExpose.CName),
                            arguments.Select(LowerExpression).ToList());
                    }

                    var staticBaseMethodKey = $"{resolvedExpose.BaseOwner}.{resolvedExpose.SourceName}";
                    if (_methodNames.TryGetValue(staticBaseMethodKey, out var staticBaseCName))
                    {
                        return new CCallExpression(
                            new CResolvedFunction(GetFunctionModule(resolvedExpose.BaseOwner, resolvedExpose.SourceName) ?? resolvedExpose.BaseOwner, staticBaseCName),
                            arguments.Select(LowerExpression).ToList());
                    }
                }

                var staticGenericCall = FindInferredGenericCall(target, member.MemberName, arguments, skipSelf: false);
                if (staticGenericCall is not null)
                {
                    return new CCallExpression(
                        new CResolvedFunction(ModuleName: GetFunctionModule(staticGenericCall.OwnerType, staticGenericCall.Name), staticGenericCall.CName),
                        arguments.Select(LowerExpression).ToList());
                }

                var staticMethodKey = $"{target}.{member.MemberName}";
                if (_methodNames.TryGetValue(staticMethodKey, out var staticCName))
                {
                    return new CCallExpression(
                        new CResolvedFunction(target, staticCName),
                        arguments.Select(LowerExpression).ToList());
                }

                return IsModuleQualifierTarget(target)
                    ? new CCallExpression(
                        new CFunctionName(member.MemberName),
                        arguments.Select(LowerExpression).ToList())
                    : null;
            }

            var normalizedType = NormalizeType(targetType);
            var isPointer = targetType.EndsWith("*", StringComparison.Ordinal);
            if (_interfaces.TryGetValue(normalizedType, out var interfaceNode)
                && interfaceNode.Methods.Any(method => method.Name == member.MemberName))
            {
                var access = isPointer ? "->" : ".";
                var vtableAccess = isPointer ? "->" : ".";
                var loweredArguments = arguments.Select(LowerExpression).ToList();
                loweredArguments.Insert(0, new CMemberExpression(new CNameExpression(target), access, "state"));
                return new CCallExpression(
                    new CFunctionName($"{target}{vtableAccess}vtable->{member.MemberName}"),
                    loweredArguments);
            }

            var receiverArgument = isPointer ? target : "&" + target;
            var receiverArguments = TryParseGenericUse(RemovePointer(normalizedType), out _, out var parsedReceiverArguments)
                ? parsedReceiverArguments
                : [];
            var adapterName = GetGenericBaseName(RemovePointer(normalizedType)) ?? RemovePointer(normalizedType);
            var receiverApiType = target == "self" && SelfApiType is not null
                ? SelfApiType
                : RemovePointer(normalizedType);
            var receiverApiArguments = TryParseGenericUse(receiverApiType, out _, out var parsedReceiverApiArguments)
                ? parsedReceiverApiArguments
                : [];
            var receiverApiName = GetGenericBaseName(receiverApiType) ?? receiverApiType;
            if (_adapterExposes.TryGetValue($"{receiverApiName}.{member.MemberName}", out var apiSelfAdapterExpose)
                && LowerAdapterExposeCall(apiSelfAdapterExpose, receiverApiArguments, arguments, target, isPointer) is { } apiSelfExposeCall)
            {
                return apiSelfExposeCall;
            }

            if (_adapterExposes.TryGetValue($"{adapterName}.{member.MemberName}", out var adapterExpose)
                && LowerAdapterExposeCall(adapterExpose, receiverArguments, arguments, target, isPointer) is { } adapterExposeCall)
            {
                return adapterExposeCall;
            }

            var genericMemberCall = FindInferredGenericCall(
                GetGenericBaseName(RemovePointer(normalizedType)) ?? RemovePointer(normalizedType),
                member.MemberName,
                arguments,
                skipSelf: true,
                preferredTypeArguments: receiverArguments);
            if (genericMemberCall is not null)
            {
                var receiver = genericMemberCall.TakesPointerSelf || isPointer
                    ? receiverArgument
                    : target;
                var loweredArguments = arguments.Select(LowerExpression).ToList();
                loweredArguments.Insert(0, ReceiverExpression(target, isPointer, genericMemberCall.TakesPointerSelf));
                return new CCallExpression(
                    new CResolvedFunction(ModuleName: GetFunctionModule(genericMemberCall.OwnerType, genericMemberCall.Name), genericMemberCall.CName),
                    loweredArguments);
            }

            foreach (var (methodName, cName) in _methodNames.Where(method =>
                !_staticMethodNames.Contains(method.Key)
                && (method.Key.StartsWith(normalizedType + ".", StringComparison.Ordinal)
                    || _methodReceiverTypes.GetValueOrDefault(method.Key) == normalizedType)))
            {
                var method = methodName[(methodName.LastIndexOf('.') + 1)..];
                if (method != member.MemberName)
                {
                    continue;
                }

                var receiver = _methodTakesPointerSelf.GetValueOrDefault(methodName)
                    ? receiverArgument
                    : target;
                var loweredArguments = arguments.Select(LowerExpression).ToList();
                loweredArguments.Insert(0, ReceiverExpression(target, isPointer, _methodTakesPointerSelf.GetValueOrDefault(methodName)));
                return new CCallExpression(
                    new CResolvedFunction(NormalizeType(targetType), cName),
                    loweredArguments);
            }

            if (TryFindAdapterExpose(adapterName, RemovePointer(normalizedType), receiverArguments, member.MemberName) is { } fallbackAdapterExpose)
            {
                return LowerAdapterExposeCall(fallbackAdapterExpose, receiverArguments, arguments, target, isPointer);
            }

            return null;
        }

        private CExpression? TryLowerKnownMemberCallExpression(
            MemberExpressionNode member,
            IReadOnlyList<ExpressionNode> arguments)
        {
            if (member.Target is NameExpressionNode)
            {
                return null;
            }

            var targetType = ResolveExpressionType(member.Target);
            if (targetType is null)
            {
                return null;
            }

            var normalizedType = NormalizeType(targetType);
            var isPointer = targetType.EndsWith("*", StringComparison.Ordinal);
            var receiverType = RemovePointer(normalizedType);
            var receiverArguments = TryParseGenericUse(receiverType, out _, out var parsedReceiverArguments)
                ? parsedReceiverArguments
                : [];
            var ownerType = GetGenericBaseName(receiverType) ?? receiverType;

            var genericMemberCall = FindInferredGenericCall(
                ownerType,
                member.MemberName,
                arguments,
                skipSelf: true,
                preferredTypeArguments: receiverArguments);
            if (genericMemberCall is not null)
            {
                var targetExpression = LowerExpression(member.Target);
                var loweredArguments = arguments.Select(LowerExpression).ToList();
                loweredArguments.Insert(0, ReceiverExpression(targetExpression, isPointer, genericMemberCall.TakesPointerSelf));
                return new CCallExpression(
                    new CResolvedFunction(ModuleName: GetFunctionModule(genericMemberCall.OwnerType, genericMemberCall.Name), genericMemberCall.CName),
                    loweredArguments);
            }

            foreach (var (methodName, cName) in _methodNames.Where(method =>
                !_staticMethodNames.Contains(method.Key)
                && (method.Key.StartsWith(receiverType + ".", StringComparison.Ordinal)
                    || _methodReceiverTypes.GetValueOrDefault(method.Key) == receiverType)))
            {
                var method = methodName[(methodName.LastIndexOf('.') + 1)..];
                if (method != member.MemberName)
                {
                    continue;
                }

                var takesPointerSelf = _methodTakesPointerSelf.GetValueOrDefault(methodName);
                var targetExpression = LowerExpression(member.Target);
                var loweredArguments = arguments.Select(LowerExpression).ToList();
                loweredArguments.Insert(0, ReceiverExpression(targetExpression, isPointer, takesPointerSelf));
                return new CCallExpression(
                    new CResolvedFunction(receiverType, cName),
                    loweredArguments);
            }

            return null;
        }

        private CExpression? LowerAdapterExposeCall(
            AdapterExposeInfo adapterExpose,
            IReadOnlyList<string> receiverArguments,
            IReadOnlyList<ExpressionNode> arguments,
            string target,
            bool isPointer)
        {
            var resolvedExpose = ResolveAdapterExpose(adapterExpose, receiverArguments);
            var genericBaseCall = FindInferredGenericCall(
                resolvedExpose.BaseOwner,
                resolvedExpose.SourceName,
                arguments,
                skipSelf: true,
                preferredTypeArguments: resolvedExpose.TypeArguments)
                ?? _genericCalls.FirstOrDefault(call =>
                    string.Equals(call.OwnerType, resolvedExpose.BaseOwner, StringComparison.Ordinal)
                    && call.Name == resolvedExpose.SourceName
                    && SameTypeArguments(call.TypeArguments, resolvedExpose.TypeArguments));
            if (genericBaseCall is not null)
            {
                var loweredArguments = arguments.Select(LowerExpression).ToList();
                loweredArguments.Insert(0, ReceiverExpression(target, isPointer, takesPointerSelf: true));
                return new CCallExpression(
                    new CResolvedFunction(ModuleName: GetFunctionModule(genericBaseCall.OwnerType, genericBaseCall.Name), genericBaseCall.CName),
                    loweredArguments);
            }

            var baseMethodKey = $"{resolvedExpose.BaseOwner}.{resolvedExpose.SourceName}";
            if (_methodNames.TryGetValue(baseMethodKey, out var baseCName))
            {
                var loweredArguments = arguments.Select(LowerExpression).ToList();
                loweredArguments.Insert(0, ReceiverExpression(target, isPointer, takesPointerSelf: true));
                return new CCallExpression(
                    new CResolvedFunction(GetFunctionModule(resolvedExpose.BaseOwner, resolvedExpose.SourceName) ?? resolvedExpose.BaseOwner, baseCName),
                    loweredArguments);
            }

            return null;
        }

        private CExpression? TryLowerInterfaceMemberCallExpression(
            MemberExpressionNode member,
            IReadOnlyList<ExpressionNode> arguments)
        {
            var targetType = ResolveExpressionType(member.Target);
            if (targetType is null)
            {
                return null;
            }

            var normalizedType = NormalizeType(RemovePointer(targetType));
            if (!_interfaces.TryGetValue(normalizedType, out var interfaceNode)
                || !interfaceNode.Methods.Any(method => method.Name == member.MemberName))
            {
                return null;
            }

            var isPointer = targetType.EndsWith("*", StringComparison.Ordinal);
            var access = isPointer ? "->" : ".";
            var targetExpression = LowerExpression(member.Target);
            var loweredArguments = arguments.Select(LowerExpression).ToList();
            loweredArguments.Insert(0, new CMemberExpression(targetExpression, access, "state"));

            var targetText = _expressionEmitter.Emit(targetExpression);
            return new CCallExpression(
                new CFunctionName($"{targetText}{access}vtable->{member.MemberName}"),
                loweredArguments);
        }

        private CExpression ReceiverExpression(string target, bool isPointer, bool takesPointerSelf)
        {
            if (_implicitReferenceLocals.ContainsKey(target))
            {
                return takesPointerSelf
                    ? new CNameExpression(target)
                    : new CUnaryExpression("*", new CNameExpression(target));
            }

            return takesPointerSelf
                ? isPointer
                    ? new CNameExpression(target)
                    : new CUnaryExpression("&", new CNameExpression(target))
                : new CNameExpression(target);
        }

        private static CExpression ReceiverExpression(CExpression target, bool isPointer, bool takesPointerSelf) =>
            takesPointerSelf || isPointer
                ? target
                : new CUnaryExpression("&", target);

        private string EmitCallExpression(CFunctionReference function, IReadOnlyList<ExpressionNode> arguments) =>
            _expressionEmitter.Emit(new CCallExpression(
                function,
                arguments.Select(argument => new CRawExpression(Lower(argument))).ToList()));

        private static string GetFunctionModule(string? ownerType, string name) =>
            ownerType is null ? name : ownerType;

        private bool IsModuleQualifierTarget(string target) =>
            _moduleQualifiers.Any(qualifier => string.Equals(qualifier, target + ".", StringComparison.Ordinal));

        public string Lower(string expression)
        {
            if (TrySplitTopLevelSpaceship(expression, out var left, out var right))
            {
                return $"compare({Lower(left)}, {Lower(right)})";
            }

            expression = Regex.Replace(expression, @"\btrue\b", "1");
            expression = Regex.Replace(expression, @"\bfalse\b", "0");
            expression = Regex.Replace(expression, @"\bnull\b", "NULL");
            expression = LowerExplicitGenericStaticCalls(expression);

            expression = LowerTaggedUnionConstructors(expression);
            expression = LowerNamedStructInitializers(expression);
            expression = LowerNamedInterfaceInitializers(expression);
            expression = LowerStructConstructors(expression);

            foreach (var (variable, type) in _variables)
            {
                var normalizedType = NormalizeType(type);
                var argument = !type.EndsWith("*", StringComparison.Ordinal)
                    ? "&" + variable
                    : variable;

                if (_taggedUnions.TryGetValue(normalizedType, out var taggedUnion))
                {
                    foreach (var variant in taggedUnion.Variants)
                    {
                        var access = type.EndsWith("*", StringComparison.Ordinal)
                            ? $"{variable}->as.{variant.Name}"
                            : $"{variable}.as.{variant.Name}";
                        expression = Regex.Replace(
                            expression,
                            $@"\b{Regex.Escape(variable)}\.{Regex.Escape(variant.Name)}\b",
                            access);
                    }
                }

                if (_interfaces.TryGetValue(normalizedType, out var interfaceNode))
                {
                    var isPointer = type.EndsWith("*", StringComparison.Ordinal);
                    var access = isPointer ? "->" : ".";
                    var stateAccess = $"{variable}{access}state";
                    foreach (var method in interfaceNode.Methods)
                    {
                        var vMethod = $"{variable}{access}vtable->{method.Name}";
                        expression = Regex.Replace(
                            expression,
                            $@"\b{Regex.Escape(variable)}\.{Regex.Escape(method.Name)}\(\s*\)",
                            $"{vMethod}({stateAccess})");
                        expression = Regex.Replace(
                            expression,
                            $@"\b{Regex.Escape(variable)}\.{Regex.Escape(method.Name)}\(",
                            $"{vMethod}({stateAccess}, ");
                    }
                }

                var receiverType = RemovePointer(normalizedType);
                var receiverArguments = TryParseGenericUse(receiverType, out _, out var parsedReceiverArguments)
                    ? parsedReceiverArguments
                    : [];
                var receiverApiType = variable == "self" && SelfApiType is not null
                    ? SelfApiType
                    : receiverType;
                var receiverApiArguments = TryParseGenericUse(receiverApiType, out _, out var parsedReceiverApiArguments)
                    ? parsedReceiverApiArguments
                    : [];
                var receiverApiName = GetGenericBaseName(receiverApiType) ?? receiverApiType;
                expression = LowerAdapterExposeCalls(expression, variable, argument, receiverApiName, receiverApiArguments);

                var adapterName = GetGenericBaseName(receiverType) ?? receiverType;
                expression = LowerAdapterExposeCalls(expression, variable, argument, adapterName, receiverArguments);
                foreach (var expose in _adapterExposes.Values.Where(expose => !expose.IsStatic))
                {
                    var baseType = SubstituteAdapterBaseType(expose, receiverArguments);
                    if (LowerType(baseType) == LowerType(receiverType))
                    {
                        expression = LowerAdapterExposeCall(expression, variable, argument, expose, receiverArguments);
                    }
                }

                foreach (var (methodName, cName) in _methodNames.Where(method =>
                    !_staticMethodNames.Contains(method.Key)
                    && (method.Key.StartsWith(normalizedType + ".", StringComparison.Ordinal)
                        || _methodReceiverTypes.GetValueOrDefault(method.Key) == normalizedType)))
                {
                    var method = methodName[(methodName.LastIndexOf('.') + 1)..];
                    var receiver = _methodTakesPointerSelf.GetValueOrDefault(methodName) ? argument : variable;
                    expression = Regex.Replace(
                        expression,
                        $@"\b{Regex.Escape(variable)}\.{Regex.Escape(method)}\(\s*\)",
                        $"{cName}({receiver})");
                    expression = Regex.Replace(
                        expression,
                        $@"\b{Regex.Escape(variable)}\.{Regex.Escape(method)}\(",
                        $"{cName}({receiver}, ");
                }

                foreach (var genericCall in _genericCalls.Where(call => !call.IsStatic && call.OwnerType == GetGenericBaseName(type)))
                {
                    var receiver = genericCall.TakesPointerSelf ? argument : variable;
                    if (SameTypeArguments(genericCall.TypeArguments, receiverArguments))
                    {
                        var inferredCallPrefix = $@"\b{Regex.Escape(variable)}\s*\.\s*{Regex.Escape(genericCall.Name)}\s*";
                        expression = Regex.Replace(expression, inferredCallPrefix + @"\(\s*\)", $"{genericCall.CName}({receiver})");
                        expression = Regex.Replace(expression, inferredCallPrefix + @"\(", $"{genericCall.CName}({receiver}, ");
                    }

                    var callPrefix = $@"\b{Regex.Escape(variable)}\s*\.\s*{Regex.Escape(genericCall.Name)}\s*<\s*{string.Join(@"\s*,\s*", genericCall.TypeArguments.Select(Regex.Escape))}\s*>\s*";
                    expression = Regex.Replace(expression, callPrefix + @"\(\s*\)", $"{genericCall.CName}({receiver})");
                    expression = Regex.Replace(expression, callPrefix + @"\(", $"{genericCall.CName}({receiver}, ");
                }
            }

            foreach (var (methodName, cName) in _methodNames)
            {
                expression = expression.Replace(methodName, cName, StringComparison.Ordinal);
            }

            foreach (var (source, target) in _tagAliases)
            {
                expression = expression.Replace(source, target, StringComparison.Ordinal);
            }

            foreach (var (source, target) in _enumMemberAliases)
            {
                expression = expression.Replace(source, target, StringComparison.Ordinal);
            }

            foreach (var qualifier in _moduleQualifiers)
            {
                expression = expression.Replace(qualifier, string.Empty, StringComparison.Ordinal);
            }

            foreach (var (alias, original) in _symbolAliases)
            {
                expression = Regex.Replace(
                    expression,
                    $@"\b{Regex.Escape(alias)}\b",
                    original);
            }

            foreach (var parameter in _pointerParameters.OrderByDescending(name => name.Length))
            {
                expression = Regex.Replace(
                    expression,
                    $@"\b{Regex.Escape(parameter)}\.",
                    parameter + "->");
            }

            expression = LowerTaggedUnionAssignment(expression);

            return expression;
        }

        private static bool TrySplitTopLevelSpaceship(string expression, out string left, out string right)
        {
            var depth = 0;
            for (var i = 0; i <= expression.Length - 3; i++)
            {
                depth += expression[i] switch
                {
                    '(' or '[' or '{' => 1,
                    ')' or ']' or '}' => -1,
                    _ => 0,
                };

                if (depth != 0 || !expression.AsSpan(i, 3).SequenceEqual("<=>"))
                {
                    continue;
                }

                left = expression[..i].Trim();
                right = expression[(i + 3)..].Trim();
                return left.Length > 0 && right.Length > 0;
            }

            left = string.Empty;
            right = string.Empty;
            return false;
        }

        private string LowerExplicitGenericStaticCalls(string expression)
        {
            foreach (var call in _genericCalls.Where(call => call.IsStatic || call.OwnerType is null))
            {
                var sourceName = call.OwnerType is null ? call.Name : $"{call.OwnerType}.{call.Name}";
                var source = Regex.Escape(sourceName).Replace("\\.", @"\s*\.\s*")
                    + @"\s*<\s*"
                    + string.Join(@"\s*,\s*", call.TypeArguments.Select(Regex.Escape))
                    + @"\s*>\s*\(";
                expression = Regex.Replace(expression, source, call.CName + "(");
            }

            return expression;
        }

        private string LowerTaggedUnionConstructors(string expression)
        {
            foreach (var taggedUnion in _taggedUnions.Values)
            {
                if (taggedUnion.IsRaw)
                {
                    continue;
                }

                foreach (var variant in taggedUnion.Variants)
                {
                    expression = ReplaceCallExpressions(
                        expression,
                        $"{taggedUnion.Name}.{variant.Name}",
                        arguments => LowerTaggedUnionConstructor(taggedUnion, variant, arguments));
                }
            }

            return expression;
        }

        private string LowerTaggedUnionConstructor(
            TaggedUnionNode taggedUnion,
            TaggedUnionVariantNode variant,
            IReadOnlyList<string> arguments)
        {
            var payload = LowerPayloadConstructor(variant.Type, arguments);
            return $"({taggedUnion.Name}){{ .tag = {taggedUnion.Name}_Tag_{variant.Name}, .as.{variant.Name} = {payload} }}";
        }

        private string LowerStructConstructors(string expression)
        {
            foreach (var structNode in _structs.Values)
            {
                expression = ReplaceCallExpressions(
                    expression,
                    structNode.Name,
                    arguments => LowerStructConstructor(structNode, arguments));
            }

            return expression;
        }

        private string LowerAdapterExposeCalls(
            string expression,
            string variable,
            string receiver,
            string adapterName,
            IReadOnlyList<string> receiverArguments)
        {
            foreach (var expose in _adapterExposes.Values.Where(expose =>
                !expose.IsStatic
                && string.Equals(expose.AdapterName, adapterName, StringComparison.Ordinal)))
            {
                expression = LowerAdapterExposeCall(expression, variable, receiver, expose, receiverArguments);
            }

            return expression;
        }

        private string LowerAdapterExposeCall(
            string expression,
            string variable,
            string receiver,
            AdapterExposeInfo expose,
            IReadOnlyList<string> receiverArguments)
        {
            var resolvedExpose = ResolveAdapterExpose(expose, receiverArguments);
            var genericBaseCall = _genericCalls.FirstOrDefault(call =>
                string.Equals(call.OwnerType, resolvedExpose.BaseOwner, StringComparison.Ordinal)
                && call.Name == resolvedExpose.SourceName
                && SameTypeArguments(call.TypeArguments, resolvedExpose.TypeArguments));

            var cName = genericBaseCall?.CName;
            if (cName is null
                && !_methodNames.TryGetValue($"{resolvedExpose.BaseOwner}.{resolvedExpose.SourceName}", out cName))
            {
                return expression;
            }

            expression = Regex.Replace(
                expression,
                $@"\b{Regex.Escape(variable)}\.{Regex.Escape(expose.ExposedName)}\(\s*\)",
                $"{cName}({receiver})");
            return Regex.Replace(
                expression,
                $@"\b{Regex.Escape(variable)}\.{Regex.Escape(expose.ExposedName)}\(",
                $"{cName}({receiver}, ");
        }

        private string LowerNamedStructInitializers(string expression)
        {
            foreach (var structNode in _structs.Values)
            {
                expression = ReplaceBracedExpressions(
                    expression,
                    structNode.Name,
                    initializerText => LowerNamedStructInitializer(structNode, initializerText));
            }

            return expression;
        }

        private string LowerNamedStructInitializer(StructNode structNode, string initializerText)
        {
            var fields = SplitNamedInitializerFields(initializerText);
            if (fields.Count == 0)
            {
                return $"({LowerType(structNode.Name)}){{0}}";
            }

            var knownFields = structNode.Fields
                .Select(field => field.Name)
                .ToHashSet(StringComparer.Ordinal);

            var initializers = fields.Select(field =>
            {
                var prefix = knownFields.Contains(field.Name)
                    ? "." + field.Name
                    : "." + field.Name;
                return $"{prefix} = {Lower(field.Value)}";
            });

            return $"({LowerType(structNode.Name)}){{ {string.Join(", ", initializers)} }}";
        }

        private string LowerNamedInterfaceInitializers(string expression)
        {
            foreach (var interfaceNode in _interfaces.Values)
            {
                expression = ReplaceBracedExpressions(
                    expression,
                    interfaceNode.Name,
                    initializerText => LowerNamedInterfaceInitializer(interfaceNode, initializerText));
            }

            return expression;
        }

        private string LowerNamedInterfaceInitializer(InterfaceNode interfaceNode, string initializerText)
        {
            var fields = SplitNamedInitializerFields(initializerText);
            if (fields.Count == 0)
            {
                return $"({LowerType(interfaceNode.Name)}){{0}}";
            }

            var knownFields = interfaceNode.Methods
                .Select(method => method.Name)
                .Append("vtable")
                .Prepend("state")
                .ToHashSet(StringComparer.Ordinal);

            var initializers = fields.Select(field =>
            {
                var prefix = knownFields.Contains(field.Name)
                    ? "." + field.Name
                    : "." + field.Name;
                return $"{prefix} = {Lower(field.Value)}";
            });

            return $"({LowerType(interfaceNode.Name)}){{ {string.Join(", ", initializers)} }}";
        }

        private string LowerPayloadConstructor(string payloadType, IReadOnlyList<string> arguments)
        {
            var normalizedPayloadType = NormalizeType(payloadType);
            if (_structs.TryGetValue(normalizedPayloadType, out var structNode))
            {
                if (arguments.Count == 1 && InferExpressionType(arguments[0]) == normalizedPayloadType)
                {
                    return Lower(arguments[0]);
                }

                return LowerStructConstructor(structNode, arguments);
            }

            return arguments.Count == 1 ? Lower(arguments[0]) : string.Join(", ", arguments.Select(Lower));
        }

        private string LowerStructConstructor(StructNode structNode, IReadOnlyList<string> arguments)
        {
            if (arguments.Count != structNode.Fields.Count)
            {
                return $"{structNode.Name}({string.Join(", ", arguments)})";
            }

            var fields = structNode.Fields
                .Zip(arguments, (field, argument) => $".{field.Name} = {Lower(argument)}");
            return $"({structNode.Name}){{ {string.Join(", ", fields)} }}";
        }

        private static string ReplaceBracedExpressions(
            string expression,
            string callee,
            Func<string, string> replacementFactory)
        {
            var searchStart = 0;
            while (searchStart < expression.Length)
            {
                var index = expression.IndexOf(callee, searchStart, StringComparison.Ordinal);
                if (index < 0)
                {
                    break;
                }

                if (index > 0 && IsIdentifierPart(expression[index - 1]))
                {
                    searchStart = index + callee.Length;
                    continue;
                }

                var scan = index + callee.Length;
                while (scan < expression.Length && char.IsWhiteSpace(expression[scan]))
                {
                    scan++;
                }

                if (scan >= expression.Length || expression[scan] != '{')
                {
                    searchStart = index + callee.Length;
                    continue;
                }

                var closeBrace = FindMatchingBrace(expression, scan);
                if (closeBrace < 0)
                {
                    break;
                }

                var initializerText = expression[(scan + 1)..closeBrace];
                var replacement = replacementFactory(initializerText);
                expression = expression[..index] + replacement + expression[(closeBrace + 1)..];
                searchStart = index + replacement.Length;
            }

            return expression;
        }

        private static string ReplaceCallExpressions(
            string expression,
            string callee,
            Func<IReadOnlyList<string>, string> replacementFactory)
        {
            var searchStart = 0;
            while (searchStart < expression.Length)
            {
                var index = expression.IndexOf(callee + "(", searchStart, StringComparison.Ordinal);
                if (index < 0)
                {
                    break;
                }

                if (index > 0 && IsIdentifierPart(expression[index - 1]))
                {
                    searchStart = index + callee.Length;
                    continue;
                }

                var openParen = index + callee.Length;
                var closeParen = FindMatchingParen(expression, openParen);
                if (closeParen < 0)
                {
                    break;
                }

                var argumentsText = expression[(openParen + 1)..closeParen];
                var arguments = SplitArguments(argumentsText);
                var replacement = replacementFactory(arguments);
                expression = expression[..index] + replacement + expression[(closeParen + 1)..];
                searchStart = index + replacement.Length;
            }

            return expression;
        }

        private static int FindMatchingBrace(string text, int openBrace)
        {
            var depth = 0;
            for (var i = openBrace; i < text.Length; i++)
            {
                if (text[i] == '{')
                {
                    depth++;
                    continue;
                }

                if (text[i] != '}')
                {
                    continue;
                }

                depth--;
                if (depth == 0)
                {
                    return i;
                }
            }

            return -1;
        }

        private static int FindMatchingParen(string text, int openParen)
        {
            var depth = 0;
            for (var i = openParen; i < text.Length; i++)
            {
                if (text[i] == '(')
                {
                    depth++;
                    continue;
                }

                if (text[i] != ')')
                {
                    continue;
                }

                depth--;
                if (depth == 0)
                {
                    return i;
                }
            }

            return -1;
        }

        private static IReadOnlyList<NamedInitializerField> SplitNamedInitializerFields(string initializerText)
        {
            var arguments = SplitArguments(initializerText);
            var fields = new List<NamedInitializerField>();

            foreach (var argument in arguments)
            {
                var colon = FindTopLevelColon(argument);
                if (colon <= 0)
                {
                    continue;
                }

                var name = argument[..colon].Trim();
                var value = argument[(colon + 1)..].Trim();
                if (name.Length == 0 || value.Length == 0)
                {
                    continue;
                }

                fields.Add(new NamedInitializerField(name, value));
            }

            return fields;
        }

        private static int FindTopLevelColon(string text)
        {
            var depth = 0;
            for (var i = 0; i < text.Length; i++)
            {
                depth += text[i] switch
                {
                    '(' or '[' or '{' => 1,
                    ')' or ']' or '}' => -1,
                    _ => 0
                };

                if (text[i] == ':' && depth == 0)
                {
                    return i;
                }
            }

            return -1;
        }

        private static IReadOnlyList<string> SplitArguments(string argumentsText)
        {
            if (string.IsNullOrWhiteSpace(argumentsText))
            {
                return [];
            }

            var arguments = new List<string>();
            var start = 0;
            var depth = 0;

            for (var i = 0; i < argumentsText.Length; i++)
            {
                depth += argumentsText[i] switch
                {
                    '(' or '[' or '{' => 1,
                    ')' or ']' or '}' => -1,
                    _ => 0
                };

                if (argumentsText[i] != ',' || depth != 0)
                {
                    continue;
                }

                arguments.Add(argumentsText[start..i].Trim());
                start = i + 1;
            }

            arguments.Add(argumentsText[start..].Trim());
            return arguments;
        }

        private static bool IsIdentifierPart(char ch) => char.IsLetterOrDigit(ch) || ch == '_';

        private string LowerTaggedUnionAssignment(string expression)
        {
            var match = Regex.Match(expression, @"^(?<target>[A-Za-z_][A-Za-z0-9_]*)\s*=\s*(?<value>.+)$");
            if (!match.Success)
            {
                return expression;
            }

            var targetName = match.Groups["target"].Value;
            if (!_variables.TryGetValue(targetName, out var targetType))
            {
                return expression;
            }

            var value = match.Groups["value"].Value.Trim();
            var wrappedValue = TryWrapTaggedUnionValue(targetType, value, value);
            return wrappedValue is null ? expression : $"{targetName} = {wrappedValue}";
        }

        private string? TryWrapTaggedUnionValue(string targetType, string sourceExpression, string loweredExpression)
        {
            var normalizedTargetType = NormalizeType(targetType);
            if (!_taggedUnions.TryGetValue(normalizedTargetType, out var taggedUnion))
            {
                return null;
            }

            if (taggedUnion.IsRaw)
            {
                return null;
            }

            var expressionType = InferExpressionType(sourceExpression);
            if (expressionType is null)
            {
                return null;
            }

            var matchingVariants = taggedUnion.Variants
                .Where(variant => LowerType(variant.Type) == LowerType(expressionType))
                .ToList();

            if (matchingVariants.Count != 1)
            {
                return null;
            }

            var variant = matchingVariants[0];
            return $"({taggedUnion.Name}){{ .tag = {taggedUnion.Name}_Tag_{variant.Name}, .as.{variant.Name} = {loweredExpression} }}";
        }

        private CExpression? TryWrapTaggedUnionValueExpression(
            string targetType,
            string sourceExpression,
            CExpression loweredExpression)
        {
            var normalizedTargetType = NormalizeType(targetType);
            if (!_taggedUnions.TryGetValue(normalizedTargetType, out var taggedUnion)
                || taggedUnion.IsRaw)
            {
                return null;
            }

            var expressionType = InferExpressionType(sourceExpression);
            if (expressionType is null)
            {
                return null;
            }

            var matchingVariants = taggedUnion.Variants
                .Where(variant => LowerType(variant.Type) == LowerType(expressionType))
                .ToList();

            if (matchingVariants.Count != 1)
            {
                return null;
            }

            var variant = matchingVariants[0];
            return new CInitializerExpression(
                LowerType(taggedUnion.Name, SelfType),
                [
                    new CInitializerField("tag", new CNameExpression($"{taggedUnion.Name}_Tag_{variant.Name}")),
                    new CInitializerField("as." + variant.Name, loweredExpression),
                ],
                []);
        }

        private string? InferExpressionType(string expression)
        {
            expression = expression.Trim();

            if (Regex.IsMatch(expression, @"^-?\s*\d+$"))
            {
                return "int";
            }

            if (expression.StartsWith('"'))
            {
                return "char*";
            }

            if (expression.StartsWith("&", StringComparison.Ordinal)
                && _variables.TryGetValue(expression[1..].Trim(), out var referencedType))
            {
                return referencedType + "*";
            }

            foreach (var structName in _structs.Keys)
            {
                if (expression.StartsWith(structName + "(", StringComparison.Ordinal))
                {
                    return structName;
                }

                if (expression.StartsWith(structName + " {", StringComparison.Ordinal))
                {
                    return structName;
                }
            }

            foreach (var interfaceName in _interfaces.Keys)
            {
                if (expression.StartsWith(interfaceName + " {", StringComparison.Ordinal))
                {
                    return interfaceName;
                }
            }

            return _variables.TryGetValue(expression, out var variableType)
                ? variableType
                : null;
        }

        private sealed record NamedInitializerField(string Name, string Value);

        private string ResolveAlias(string type)
        {
            var isPointer = type.EndsWith("*", StringComparison.Ordinal);
            var coreType = isPointer ? type.TrimEnd('*').TrimEnd() : type;
            var seen = new HashSet<string>(StringComparer.Ordinal);

            while (_typeAliases.TryGetValue(coreType, out var targetType) && seen.Add(coreType))
            {
                coreType = targetType;
            }

            return isPointer ? coreType + "*" : coreType;
        }

        private static string SubstituteAdapterBaseType(
            AdapterExposeInfo adapter,
            IReadOnlyList<string> receiverArguments)
        {
            if (adapter.TypeParameters.Count == 0 || adapter.TypeParameters.Count != receiverArguments.Count)
            {
                return adapter.BaseType;
            }

            var substitutions = adapter.TypeParameters
                .Zip(receiverArguments)
                .ToDictionary(pair => pair.First, pair => pair.Second, StringComparer.Ordinal);
            return SubstituteGenericType(adapter.BaseType, substitutions);
        }

        private ResolvedAdapterExpose ResolveAdapterExpose(
            AdapterExposeInfo expose,
            IReadOnlyList<string> receiverArguments)
        {
            var current = expose;
            var currentArguments = receiverArguments;
            var seen = new HashSet<string>(StringComparer.Ordinal);

            while (true)
            {
                var baseType = SubstituteAdapterBaseType(current, currentArguments);
                var baseOwner = GetGenericBaseName(baseType) ?? baseType;
                var baseArguments = TryParseGenericUse(baseType, out _, out var parsedBaseArguments)
                    ? parsedBaseArguments
                    : [];
                var key = $"{current.AdapterName}.{current.ExposedName}";
                if (!seen.Add(key)
                    || !_adapterExposes.TryGetValue($"{baseOwner}.{current.SourceName}", out var next)
                    || next.IsStatic != current.IsStatic)
                {
                    return new ResolvedAdapterExpose(baseType, baseOwner, current.SourceName, baseArguments);
                }

                current = next;
                currentArguments = baseArguments;
            }
        }

        private AdapterExposeInfo? TryFindAdapterExpose(
            string adapterName,
            string storageType,
            IReadOnlyList<string> receiverArguments,
            string exposedName)
        {
            if (_adapterExposes.TryGetValue($"{adapterName}.{exposedName}", out var direct))
            {
                return direct;
            }

            foreach (var expose in _adapterExposes.Values)
            {
                if (expose.ExposedName != exposedName || expose.IsStatic)
                {
                    continue;
                }

                var baseType = SubstituteAdapterBaseType(expose, receiverArguments);
                if (LowerType(baseType) == LowerType(storageType))
                {
                    return expose;
                }
            }

            return null;
        }

        public sealed record MatchInfo(
            string TagAccess,
            TaggedUnionNode? Union,
            string? PayloadAccess,
            InterfaceNode? Interface,
            IReadOnlyDictionary<string, InterfaceImplementation> InterfaceImplementations,
            string? StateAccess)
        {
            public static MatchInfo ForTaggedUnion(
                string unionName,
                TaggedUnionNode union,
                string tagAccess,
                string payloadAccess) =>
                new(tagAccess, union, payloadAccess, null, new Dictionary<string, InterfaceImplementation>(), null);

            public static MatchInfo ForInterface(
                InterfaceNode interfaceNode,
                string tagAccess,
                string stateAccess,
                IReadOnlyDictionary<string, InterfaceImplementation> implementations) =>
                new(tagAccess, null, null, interfaceNode, implementations, stateAccess);

            public string CaseLabel(string pattern) =>
                Union is not null
                    ? $"{Union.Name}_Tag_{pattern}"
                    : GetTypeIdName(pattern);

            public string? BindingType(string pattern)
            {
                if (Union is not null)
                {
                    return Union.Variants.FirstOrDefault(variant => variant.Name == pattern)?.Type;
                }

                return InterfaceImplementations.ContainsKey(pattern)
                    ? pattern + "*"
                    : null;
            }

            public CExpression? BindingExpression(string pattern)
            {
                if (Union is not null && PayloadAccess is not null)
                {
                    return ToCSimpleAccessExpression($"{PayloadAccess}.{pattern}");
                }

                if (Interface is not null
                    && StateAccess is not null
                    && InterfaceImplementations.ContainsKey(pattern))
                {
                    return new CCastExpression(LowerType(pattern) + "*", ToCSimpleAccessExpression(StateAccess));
                }

                return null;
            }
        }

