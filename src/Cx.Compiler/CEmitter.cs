    private static void EmitStatement(
        StringBuilder builder,
        StatementNode statement,
        ImportedNameLowerer nameLowerer,
        int indentLevel)
    {
        switch (statement)
        {
            case LetStatement let:
                AppendIndent(builder, indentLevel);
                builder.AppendLine(EmitLetStatement(let, nameLowerer));
                break;
            case ReturnStatement ret:
                AppendIndent(builder, indentLevel);
                builder.AppendLine(ret.Expression is null
                    ? "return;"
                    : $"return {nameLowerer.Lower(ret.Expression)};");
                break;
            case BreakStatement:
                AppendIndent(builder, indentLevel);
                builder.AppendLine("break;");
                break;
            case ContinueStatement:
                AppendIndent(builder, indentLevel);
                builder.AppendLine("continue;");
                break;
            case CStatement c:
                AppendIndent(builder, indentLevel);
                builder.AppendLine($"{nameLowerer.Lower(c.Expression)};");
                break;
            case IfStatement ifStatement:
                EmitIfStatement(builder, ifStatement, nameLowerer, indentLevel);
                break;
            case WhileStatement whileStatement:
                AppendIndent(builder, indentLevel);
                builder.AppendLine($"while ({nameLowerer.Lower(whileStatement.Condition)})");
                EmitBlock(builder, whileStatement.Body, nameLowerer, indentLevel);
                break;
            case ForStatement forStatement:
                AppendIndent(builder, indentLevel);
                builder.AppendLine(
                    $"for ({EmitForInitializer(forStatement.Initializer, nameLowerer)}; {nameLowerer.Lower(forStatement.Condition)}; {nameLowerer.Lower(forStatement.Increment)})");
                EmitBlock(builder, forStatement.Body, nameLowerer, indentLevel);
                break;
            case ForeachStatement foreachStatement:
                EmitForeachStatement(builder, foreachStatement, nameLowerer, indentLevel);
                break;
            case SwitchStatement switchStatement:
                EmitSwitchStatement(builder, switchStatement, nameLowerer, indentLevel);
                break;
            case MatchStatement matchStatement:
                EmitMatchStatement(builder, matchStatement, nameLowerer, indentLevel);
                break;
            default:
                throw new InvalidOperationException($"Unknown statement node {statement.GetType().Name}.");
        }
    }

    private static string EmitLetStatement(LetStatement let, ImportedNameLowerer nameLowerer) => let switch
    {
        LetStatement declaration when declaration.Initializer is null => $"{(declaration.IsConst ? "const " : "")}{LowerDeclaration(declaration.TypeNode, LetStatementTypeText(declaration), declaration.Name, nameLowerer.SelfType)};",
        LetStatement declaration => $"{(declaration.IsConst ? "const " : "")}{LowerDeclaration(declaration.TypeNode, LetStatementTypeText(declaration), declaration.Name, nameLowerer.SelfType)} = {nameLowerer.LowerInitializer(LetStatementTypeText(declaration), declaration.Initializer!)};",
    };

    private static string EmitForInitializer(ForInitializerNode initializer, ImportedNameLowerer nameLowerer) => initializer switch
    {
        ForDeclarationInitializerNode declaration when declaration.Initializer is null =>
            $"{(declaration.IsConst ? "const " : "")}{LowerDeclaration(declaration.TypeNode, ForDeclarationInitializerTypeText(declaration), declaration.Name, nameLowerer.SelfType)}",
        ForDeclarationInitializerNode declaration =>
            $"{(declaration.IsConst ? "const " : "")}{LowerDeclaration(declaration.TypeNode, ForDeclarationInitializerTypeText(declaration), declaration.Name, nameLowerer.SelfType)} = {nameLowerer.LowerInitializer(ForDeclarationInitializerTypeText(declaration), declaration.Initializer!)}",
        ForExpressionInitializerNode expression => nameLowerer.Lower(expression.Expression),
        _ => string.Empty,
    };

    private static void EmitForeachStatement(
        StringBuilder builder,
        ForeachStatement foreachStatement,
        ImportedNameLowerer nameLowerer,
        int indentLevel)
    {
        var iterableExpression = ExpressionText(foreachStatement.IterableExpression);
        if (foreachStatement.KeyBinding is not null)
        {
            AppendIndent(builder, indentLevel);
            builder.AppendLine($"/* key/value foreach lowering is not supported yet for {iterableExpression} */");
            return;
        }

        var iterable = nameLowerer.ResolveContiguousIterable(iterableExpression);
        if (iterable is null)
        {
            AppendIndent(builder, indentLevel);
            builder.AppendLine($"/* unable to lower foreach {foreachStatement.ItemName} in {iterableExpression} */");
            return;
        }

        var indexName = foreachStatement.IndexBinding?.Name ?? $"__cx_i{indentLevel}";
        var loopIndexName = foreachStatement.IndexBinding is null
            ? indexName
            : $"__cx_i{indentLevel}";
        var indexType = GetForeachIndexType(foreachStatement);
        var itemType = GetForeachValueType(foreachStatement, iterable.ElementType);
        var itemStorageType = GetForeachValueStorageType(foreachStatement, iterable.ElementType);
        AppendIndent(builder, indentLevel);
        builder.AppendLine($"for ({LowerDeclaration(indexType, loopIndexName, nameLowerer.SelfType)} = 0; {loopIndexName} < {iterable.LengthAccess}; {loopIndexName}++)");
        AppendIndent(builder, indentLevel);
        builder.AppendLine("{");
        if (foreachStatement.IndexBinding is not null)
        {
            AppendIndent(builder, indentLevel + 1);
            builder.AppendLine($"const {LowerDeclaration(indexType, indexName, nameLowerer.SelfType)} = {loopIndexName};");
        }

        AppendIndent(builder, indentLevel + 1);
        builder.AppendLine($"{LowerDeclaration(itemStorageType, foreachStatement.ItemName, nameLowerer.SelfType)} = &{iterable.DataAccess}[{loopIndexName}];");

        var bodyLowerer = nameLowerer.WithImplicitReferenceLocal(
            foreachStatement.ItemName,
            itemType,
            itemStorageType,
            foreachStatement.ValueBinding.IsConst);
        if (foreachStatement.IndexBinding is { } indexBinding)
        {
            bodyLowerer = bodyLowerer.WithLocal(indexBinding.Name, indexType);
        }

        foreach (var statement in foreachStatement.Body)
        {
            EmitStatement(builder, statement, bodyLowerer, indentLevel + 1);
        }

        AppendIndent(builder, indentLevel);
        builder.AppendLine("}");
    }

    private static void EmitSwitchStatement(
        StringBuilder builder,
        SwitchStatement switchStatement,
        ImportedNameLowerer nameLowerer,
        int indentLevel)
    {
        AppendIndent(builder, indentLevel);
        builder.AppendLine($"switch ({nameLowerer.Lower(switchStatement.Expression)})");
        AppendIndent(builder, indentLevel);
        builder.AppendLine("{");

        foreach (var switchCase in switchStatement.Cases)
        {
            AppendIndent(builder, indentLevel + 1);
            builder.AppendLine($"case {nameLowerer.Lower(switchCase.Pattern)}:");
            AppendIndent(builder, indentLevel + 1);
            builder.AppendLine("{");
            foreach (var statement in switchCase.Body)
            {
                EmitStatement(builder, statement, nameLowerer, indentLevel + 2);
            }
            AppendIndent(builder, indentLevel + 1);
            builder.AppendLine("}");
        }

        if (switchStatement.DefaultBody.Count > 0)
        {
            AppendIndent(builder, indentLevel + 1);
            builder.AppendLine("default:");
            AppendIndent(builder, indentLevel + 1);
            builder.AppendLine("{");
            foreach (var statement in switchStatement.DefaultBody)
            {
                EmitStatement(builder, statement, nameLowerer, indentLevel + 2);
            }
            AppendIndent(builder, indentLevel + 1);
            builder.AppendLine("}");
        }

        AppendIndent(builder, indentLevel);
        builder.AppendLine("}");
    }

    private static void EmitIfStatement(
        StringBuilder builder,
        IfStatement ifStatement,
        ImportedNameLowerer nameLowerer,
        int indentLevel)
    {
        AppendIndent(builder, indentLevel);
        builder.AppendLine($"if ({nameLowerer.Lower(ifStatement.Condition)})");
        EmitBlock(builder, ifStatement.ThenBody, nameLowerer, indentLevel);

        if (ifStatement.ElseBranch is IfStatement elseIf)
        {
            AppendIndent(builder, indentLevel);
            builder.Append("else ");
            builder.AppendLine($"if ({nameLowerer.Lower(elseIf.Condition)})");
            EmitBlock(builder, elseIf.ThenBody, nameLowerer, indentLevel);

            if (elseIf.ElseBranch is not null)
            {
                EmitElseBranch(builder, elseIf.ElseBranch, nameLowerer, indentLevel);
            }
        }
        else if (ifStatement.ElseBranch is not null)
        {
            EmitElseBranch(builder, ifStatement.ElseBranch, nameLowerer, indentLevel);
        }
    }

    private static void EmitElseBranch(
        StringBuilder builder,
        StatementNode elseBranch,
        ImportedNameLowerer nameLowerer,
        int indentLevel)
    {
        switch (elseBranch)
        {
            case IfStatement elseIf:
                AppendIndent(builder, indentLevel);
                builder.Append("else ");
                builder.AppendLine($"if ({nameLowerer.Lower(elseIf.Condition)})");
                EmitBlock(builder, elseIf.ThenBody, nameLowerer, indentLevel);

                if (elseIf.ElseBranch is not null)
                {
                    EmitElseBranch(builder, elseIf.ElseBranch, nameLowerer, indentLevel);
                }

                break;
            case ElseBlockStatement elseBlock:
                AppendIndent(builder, indentLevel);
                builder.AppendLine("else");
                EmitBlock(builder, elseBlock.Body, nameLowerer, indentLevel);
                break;
            default:
                throw new InvalidOperationException($"Unexpected else branch node {elseBranch.GetType().Name}.");
        }
    }

    private static void EmitBlock(
        StringBuilder builder,
        IReadOnlyList<StatementNode> statements,
        ImportedNameLowerer nameLowerer,
        int indentLevel)
    {
        AppendIndent(builder, indentLevel);
        builder.AppendLine("{");

        foreach (var statement in statements)
        {
            EmitStatement(builder, statement, nameLowerer, indentLevel + 1);
        }

        AppendIndent(builder, indentLevel);
        builder.AppendLine("}");
    }

    private static void EmitMatchStatement(
        StringBuilder builder,
        MatchStatement matchStatement,
        ImportedNameLowerer nameLowerer,
        int indentLevel)
    {
        var matchExpression = ExpressionText(matchStatement.Expression);
        var matchInfo = nameLowerer.ResolveMatch(matchExpression);
        if (matchInfo is null)
        {
            AppendIndent(builder, indentLevel);
            builder.AppendLine($"/* unable to lower match {matchExpression} */");
            return;
        }

        AppendIndent(builder, indentLevel);
        builder.AppendLine($"switch ({matchInfo.TagAccess})");
        AppendIndent(builder, indentLevel);
        builder.AppendLine("{");

        foreach (var arm in matchStatement.Arms)
        {
            if (arm.Pattern == "_")
            {
                AppendIndent(builder, indentLevel + 1);
                builder.AppendLine("default:");
            }
            else
            {
                AppendIndent(builder, indentLevel + 1);
                builder.AppendLine($"case {matchInfo.CaseLabel(arm.Pattern)}:");
            }

            AppendIndent(builder, indentLevel + 1);
            builder.AppendLine("{");

            if (arm.BindingName is not null && arm.Pattern != "_")
            {
                if (matchInfo.BindingType(arm.Pattern) is { } bindingType
                    && matchInfo.BindingExpression(arm.Pattern) is { } bindingExpression)
                {
                    AppendIndent(builder, indentLevel + 2);
                    builder.AppendLine($"{LowerDeclaration(bindingType, arm.BindingName, nameLowerer.SelfType)} = {new CExpressionEmitter().Emit(bindingExpression)};");
                    nameLowerer = nameLowerer.WithLocal(arm.BindingName, bindingType);
                }
            }

            foreach (var statement in arm.Body)
            {
                EmitStatement(builder, statement, nameLowerer, indentLevel + 2);
            }

            if (!ArmAlwaysTransfers(arm.Body))
            {
                AppendIndent(builder, indentLevel + 2);
                builder.AppendLine("break;");
            }

            AppendIndent(builder, indentLevel + 1);
            builder.AppendLine("}");
        }

        AppendIndent(builder, indentLevel);
        builder.AppendLine("}");
    }

    private static bool ArmAlwaysTransfers(IReadOnlyList<StatementNode> body) =>
        body.LastOrDefault() is ReturnStatement;

    private static int FindMatchingGenericClose(string type, int openIndex)
    {
        var depth = 0;
        for (var i = openIndex; i < type.Length; i++)
        {
            if (type[i] == '<')
            {
                depth++;
                continue;
            }

            if (type[i] != '>')
            {
                continue;
            }

            depth--;
            if (depth == 0)
            {
                return i;
            }
        }

        return -1;
    }

    private static IReadOnlyList<string> SplitGenericArguments(string argumentsText)
    {
        if (string.IsNullOrWhiteSpace(argumentsText))
        {
            return [];
        }

        var arguments = new List<string>();
        var start = 0;
        var angleDepth = 0;
        var parenDepth = 0;
        var bracketDepth = 0;

        for (var i = 0; i < argumentsText.Length; i++)
        {
            switch (argumentsText[i])
            {
                case '<': angleDepth++; break;
                case '>': angleDepth--; break;
                case '(': parenDepth++; break;
                case ')': parenDepth--; break;
                case '[': bracketDepth++; break;
                case ']': bracketDepth--; break;
            }

            if (argumentsText[i] != ',' || angleDepth != 0 || parenDepth != 0 || bracketDepth != 0)
            {
                continue;
            }

            arguments.Add(argumentsText[start..i].Trim());
            start = i + 1;
        }

        arguments.Add(argumentsText[start..].Trim());
        return arguments;
    }

    private static string SubstituteGenericType(string type, IReadOnlyDictionary<string, string> substitutions)
    {
        foreach (var (parameter, argument) in substitutions)
        {
            type = Regex.Replace(type, $@"\b{Regex.Escape(parameter)}\b", argument);
        }

        return type;
    }

