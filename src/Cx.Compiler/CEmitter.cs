        public ImportedNameLowerer ForFunction(FunctionNode function)
        {
            var selfType = ResolveSelfType(function);
            var selfApiType = ResolveSelfApiType(function);
            var variables = _variables.ToDictionary(StringComparer.Ordinal);
            foreach (var parameter in function.Parameters
                .Where(parameter => !parameter.IsVariadic)
                .Select(parameter => (parameter.Name, Type: SubstituteSelfType(parameter.Type, selfType)))
                .Concat(CollectLocalVariables(function.Body)
                    .Select(statement => (statement.Name, Type: SubstituteSelfType(statement.Type, selfType))))
                .Where(item => !string.IsNullOrWhiteSpace(item.Name) && !string.IsNullOrWhiteSpace(item.Type))
                .GroupBy(item => item.Name, StringComparer.Ordinal)
                .Select(group => (group.Key, Type: group.First().Type)))
            {
                variables[parameter.Key] = parameter.Type;
            }
            var pointerVariables = variables
                .Where(item => item.Value.EndsWith("*", StringComparison.Ordinal))
                .Select(item => item.Key)
                .ToHashSet(StringComparer.Ordinal);

            return new(
                _symbolAliases,
                _moduleQualifiers,
                _methodNames,
                _methodReceiverTypes,
                _methodTakesPointerSelf,
                _genericCalls,
                _genericMacroNames,
                _staticMethodNames,
                _structs,
                _interfaces,
                _interfaceImplementations,
                _taggedUnions,
                _tagAliases,
                _enumMemberAliases,
                _typeAliases,
                _adapterExposes,
                _requirementMatcher,
                pointerVariables,
                variables,
                _implicitReferenceLocals,
                selfType,
                selfApiType);
        }

