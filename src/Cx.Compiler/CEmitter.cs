using System.Text;
using System.Text.RegularExpressions;
using Cx.Compiler.C;
using Cx.Compiler.Lowering;
using Cx.Compiler.Semantic;
using Cx.Compiler.Syntax.Nodes;

namespace Cx.Compiler;

public sealed class CEmitter
{
    private static IReadOnlyList<TypeAdapterNode> s_typeAdapters = [];
    private static CNameMangler s_nameMangler = CreateNameMangler();

    public CEmitter()
        : this(null)
    {
    }

    internal CEmitter(CNameManglerOptions? nameManglerOptions)
    {
        s_nameMangler = CreateNameMangler(nameManglerOptions);
    }

    public string Emit(ProgramNode program) =>
        Emit(LowerToC(program));

    internal string Emit(CTranslationUnit unit) =>
        new CTranslationUnitEmitter().Emit(unit);

    internal CTranslationUnit LowerToC(ProgramNode program)
    {
        s_nameMangler = CreateNameMangler(s_nameMangler.Options);
        s_typeAdapters = program.TypeAdapters;
        var items = new List<CTranslationUnitItem>
        {
            new CComment("generated by Cx.Compiler"),
            new CBlankLine(),
        };

        var functionsToEmit = GetFunctionsToEmit(program);
        var emitProgram = program with { Functions = functionsToEmit };
        var includes = program.Includes
            .Concat(GetCDeclarationsToInclude(emitProgram)
                .Select(declaration => new IncludeNode(declaration.Location, declaration.HeaderPath, declaration.IsSystemHeader)))
            .DistinctBy(include => (include.Path, include.IsSystem))
            .ToList();
        if (UsesNull(emitProgram) && !includes.Any(include => include.IsSystem && include.Path == "stddef.h"))
        {
            items.Add(new CInclude("stddef.h", IsSystem: true));
        }

        foreach (var include in includes)
        {
            items.Add(new CInclude(include.Path, include.IsSystem));
        }

        if (includes.Count > 0 || UsesNull(emitProgram))
        {
            items.Add(new CBlankLine());
        }

        foreach (var externFunction in program.ExternFunctions.Where(function => !function.IsHeaderDeclaration))
        {
            items.Add(ToCFunctionDeclaration(externFunction));
        }

        if (program.ExternFunctions.Any(function => !function.IsHeaderDeclaration))
        {
            items.Add(new CBlankLine());
        }

        var structsToEmit = GetStructsToEmit(emitProgram);
        var compositeTypeNames = structsToEmit
            .Select(structNode => structNode.Name)
            .Concat(emitProgram.TaggedUnions.Where(union => !union.IsHeaderDeclaration).Select(taggedUnion => taggedUnion.Name))
            .ToHashSet(StringComparer.Ordinal);
        var emittedTypeAliases = program.TypeAliases
            .Where(typeAlias => !typeAlias.IsHeaderDeclaration)
            .ToList();
        var earlyTypeAliases = emittedTypeAliases
            .Where(typeAlias => !ReferencesCompositeType(typeAlias.TargetType, compositeTypeNames))
            .ToList();
        var lateTypeAliases = emittedTypeAliases
            .Where(typeAlias => ReferencesCompositeType(typeAlias.TargetType, compositeTypeNames))
            .ToList();

        foreach (var typeAlias in earlyTypeAliases)
        {
            items.Add(ToCTypeAlias(typeAlias));
        }

        if (earlyTypeAliases.Count > 0)
        {
            items.Add(new CBlankLine());
        }

        foreach (var enumNode in emitProgram.Enums.Where(enumNode => !enumNode.IsHeaderDeclaration))
        {
            items.Add(ToCEnum(enumNode));
            items.Add(new CBlankLine());
        }

        var interfaceImplementations = GetInterfaceImplementations(emitProgram, structsToEmit);
        if (emitProgram.Interfaces.Count > 0)
        {
            items.Add(ToCTypeIdEnum(interfaceImplementations));
            items.Add(new CBlankLine());
        }

        foreach (var interfaceNode in emitProgram.Interfaces)
        {
            items.Add(ToCInterfaceVTableStruct(interfaceNode));
            items.Add(new CBlankLine());
            items.Add(ToCInterfaceValueStruct(interfaceNode));
            items.Add(new CBlankLine());
        }

        var taggedUnionNames = emitProgram.TaggedUnions
            .Where(union => !union.IsRaw)
            .Select(taggedUnion => taggedUnion.Name)
            .ToHashSet(StringComparer.Ordinal);
        var lateStructNames = structsToEmit
            .Where(structNode => structNode.Fields.Any(field => ReferencesCompositeType(field.Type, taggedUnionNames)))
            .Select(structNode => structNode.Name)
            .ToHashSet(StringComparer.Ordinal);
        var changed = true;
        while (changed)
        {
            changed = false;
            foreach (var structNode in structsToEmit)
            {
                if (lateStructNames.Contains(structNode.Name)
                    || !structNode.Fields.Any(field => ReferencesCompositeType(field.Type, lateStructNames)))
                {
                    continue;
                }

                lateStructNames.Add(structNode.Name);
                changed = true;
            }
        }

        var earlyStructs = OrderStructsByFieldDependencies(
            structsToEmit.Where(structNode => !lateStructNames.Contains(structNode.Name)).ToList());
        var lateStructs = OrderStructsByFieldDependencies(
            structsToEmit.Where(structNode => lateStructNames.Contains(structNode.Name)).ToList());

        foreach (var structNode in earlyStructs)
        {
            items.Add(ToCStruct(structNode));
            items.Add(new CBlankLine());
        }

        foreach (var taggedUnion in emitProgram.TaggedUnions.Where(union => !union.IsHeaderDeclaration))
        {
            items.Add(ToCTaggedUnion(taggedUnion));
            items.Add(new CBlankLine());
        }

        foreach (var structNode in lateStructs)
        {
            items.Add(ToCStruct(structNode));
            items.Add(new CBlankLine());
        }

        foreach (var typeAlias in lateTypeAliases)
        {
            items.Add(ToCTypeAlias(typeAlias));
        }

        if (lateTypeAliases.Count > 0)
        {
            items.Add(new CBlankLine());
        }

        var requirementMatcher = new RequirementMatcher(emitProgram, structsToEmit);
        var nameLowerer = new ImportedNameLowerer(emitProgram, structsToEmit, requirementMatcher);

        foreach (var implementation in interfaceImplementations)
        {
            items.Add(ToCInterfaceVTableDeclaration(implementation));
        }

        if (interfaceImplementations.Count > 0)
        {
            items.Add(new CBlankLine());
        }

        foreach (var global in emitProgram.GlobalVariables.Where(global => !global.IsHeaderDeclaration))
        {
            items.Add(ToCGlobalDeclaration(global, nameLowerer));
        }

        if (emitProgram.GlobalVariables.Any(global => !global.IsHeaderDeclaration))
        {
            items.Add(new CBlankLine());
        }

        foreach (var function in functionsToEmit)
        {
            items.Add(ToCFunctionDeclaration(function));
        }

        if (program.Functions.Count > 0)
        {
            items.Add(new CBlankLine());
        }

        foreach (var implementation in interfaceImplementations)
        {
            items.Add(ToCInterfaceVTableInstance(implementation, functionsToEmit));
            items.Add(new CBlankLine());
        }

        foreach (var function in functionsToEmit)
        {
            items.Add(ToCFunctionDefinition(function, nameLowerer.ForFunction(function)));
            items.Add(new CBlankLine());
        }

        return new CTranslationUnit(items);
    }

    private static CNameMangler CreateNameMangler(CNameManglerOptions? options = null) =>
        new(
            type => LowerType(type),
            SanitizeTypeName,
            options);

    private static void AddRaw(ICollection<CTranslationUnitItem> items, Action<StringBuilder> render)
    {
        var builder = new StringBuilder();
        render(builder);
        if (builder.Length > 0)
        {
            items.Add(new CRawTopLevel(builder.ToString()));
        }
    }

    private static CEnumDeclaration ToCEnum(EnumNode enumNode) =>
        new(
            enumNode.Name,
            enumNode.Members
                .Select(member => new CEnumMember(member.Name, member.Value))
                .ToList());

    private static CStructDeclaration ToCStruct(StructNode structNode) =>
        new(
            structNode.Name,
            structNode.Fields
                .Select(field => LowerStructFieldDeclaration(structNode, field))
                .ToList());

    private static CEnumDeclaration ToCTypeIdEnum(IReadOnlyList<InterfaceImplementation> implementations) =>
        new(
            "CxTypeId",
            new[] { new CEnumMember("CX_TYPE_UNKNOWN", "0") }
                .Concat(implementations
                .Select(implementation => implementation.Struct.Name)
                .Distinct(StringComparer.Ordinal)
                .Order(StringComparer.Ordinal)
                .Select(name => new CEnumMember(GetTypeIdName(name), null)))
                .ToList());

    private static CStructDeclaration ToCInterfaceVTableStruct(InterfaceNode interfaceNode)
    {
        var fields = new List<string> { "CxTypeId type_id" };
        foreach (var method in interfaceNode.Methods)
        {
            var parameters = new List<string> { "void* state" };
            parameters.AddRange(method.Parameters.Select(parameter => LowerParameterDeclaration(parameter)));
            fields.Add($"{LowerType(method.ReturnType)} (*{method.Name})({string.Join(", ", parameters)})");
        }

        return new CStructDeclaration(GetInterfaceVTableName(interfaceNode.Name), fields);
    }

    private static CStructDeclaration ToCInterfaceValueStruct(InterfaceNode interfaceNode)
    {
        var fields = new List<string>
        {
            "void* state",
            $"const {GetInterfaceVTableName(interfaceNode.Name)}* vtable",
        };
        return new CStructDeclaration(interfaceNode.Name, fields);
    }

    private static CRawTopLevel ToCInterfaceVTableInstance(
        InterfaceImplementation implementation,
        IReadOnlyList<FunctionNode> functions)
    {
        var builder = new StringBuilder();
        builder.AppendLine($"const {GetInterfaceVTableName(implementation.Interface.Name)} {GetInterfaceVTableInstanceName(implementation.Struct.Name, implementation.Interface.Name)} =");
        builder.AppendLine("{");
        builder.AppendLine($"    .type_id = {GetTypeIdName(implementation.Struct.Name)},");
        foreach (var method in implementation.Interface.Methods)
        {
            var concrete = functions.FirstOrDefault(function =>
                GetConcreteFunctionOwnerName(function) == implementation.Struct.Name
                && !function.IsStatic
                && function.Name == method.Name);
            if (concrete is null)
            {
                continue;
            }

            var parameters = new List<string> { "void*" };
            parameters.AddRange(method.Parameters.Select(parameter => LowerType(parameter.Type)));
            var slotType = $"{LowerType(method.ReturnType)} (*)({string.Join(", ", parameters)})";
            builder.AppendLine($"    .{method.Name} = ({slotType}){GetCFunctionName(concrete)},");
        }

        builder.AppendLine("};");
        return new CRawTopLevel(builder.ToString());
    }

    private static CRawTopLevel ToCInterfaceVTableDeclaration(InterfaceImplementation implementation) =>
        new($"extern const {GetInterfaceVTableName(implementation.Interface.Name)} {GetInterfaceVTableInstanceName(implementation.Struct.Name, implementation.Interface.Name)};{Environment.NewLine}");

    private static CTaggedUnionDeclaration ToCTaggedUnion(TaggedUnionNode taggedUnion) =>
        new(
            taggedUnion.Name,
            taggedUnion.IsRaw,
            taggedUnion.Variants
                .Select(variant => new CTaggedUnionVariantDeclaration(
                    variant.Name,
                    LowerType(variant.Type),
                    LowerDeclaration(variant.Type, variant.Name)))
                .ToList());

    private static IReadOnlyList<InterfaceImplementation> GetInterfaceImplementations(
        ProgramNode program,
        IReadOnlyList<StructNode> structs)
    {
        var interfaces = program.Interfaces.ToDictionary(interfaceNode => interfaceNode.Name, StringComparer.Ordinal);
        return structs
            .Where(structNode => !structNode.IsHeaderDeclaration)
            .SelectMany(structNode => structNode.Requirements
                .Select(requirement => interfaces.TryGetValue(requirement.Name, out var interfaceNode)
                    ? new InterfaceImplementation(structNode, interfaceNode)
                    : null)
                .Where(implementation => implementation is not null)
                .Cast<InterfaceImplementation>())
            .GroupBy(implementation => (implementation.Struct.Name, implementation.Interface.Name))
            .Select(group => group.First())
            .ToList();
    }

    private static string GetInterfaceVTableName(string interfaceName) =>
        $"{interfaceName}VTable";

    private static string GetInterfaceVTableInstanceName(string structName, string interfaceName) =>
        $"{structName}_{interfaceName}_vtable";

    private static string GetTypeIdName(string typeName) =>
        "CX_TYPE_" + SanitizeTypeName(LowerType(typeName));

    private static CTypeAliasDeclaration ToCTypeAlias(TypeAliasNode typeAlias)
    {
        if (TryParseFunctionType(typeAlias.TargetType, out var parameters, out var returnType))
        {
            return new CTypeAliasDeclaration(
                typeAlias.Name,
                LowerType(returnType),
                parameters.Select(parameter => LowerFunctionTypeParameter(parameter)).ToList());
        }

        return new CTypeAliasDeclaration(
            typeAlias.Name,
            LowerType(typeAlias.TargetTypeNode, typeAlias.TargetType));
    }

    private static CFunctionDeclaration ToCFunctionDeclaration(FunctionNode function)
    {
        var selfType = ResolveSelfType(function);
        return new CFunctionDeclaration(
            LowerType(function.ReturnTypeNode, function.ReturnType, selfType),
            GetCFunctionName(function),
            function.Parameters
                .Select(parameter => LowerParameterDeclaration(parameter, selfType))
                .ToList());
    }

    private static CFunctionDefinition ToCFunctionDefinition(
        FunctionNode function,
        ImportedNameLowerer nameLowerer)
    {
        var declaration = ToCFunctionDeclaration(function);
        return new CFunctionDefinition(
            declaration.ReturnType,
            declaration.Name,
            declaration.ParameterDeclarations,
            function.Body.Select(statement => ToCStatement(statement, nameLowerer, rawIndentLevel: 1)).ToList());
    }

    private static CStatementNode ToCStatement(
        StatementNode statement,
        ImportedNameLowerer nameLowerer,
        int rawIndentLevel) => statement switch
    {
        LetStatement let => ToCLocalDeclaration(let, nameLowerer),
        ReturnStatement ret => new CReturnStatement(nameLowerer.LowerExpression(ret.Expression)),
        BreakStatement => new CBreakStatement(),
        ContinueStatement => new CContinueStatement(),
        CStatement c => new CExpressionStatement(nameLowerer.LowerExpression(c.Expression)),
        IfStatement ifStatement => ToCIfStatement(ifStatement, nameLowerer, rawIndentLevel),
        WhileStatement whileStatement => ToCWhileStatement(whileStatement, nameLowerer, rawIndentLevel),
        ForStatement forStatement => ToCForStatement(forStatement, nameLowerer, rawIndentLevel),
        ForeachStatement foreachStatement => ToCForeachStatement(foreachStatement, nameLowerer, rawIndentLevel),
        SwitchStatement switchStatement => ToCSwitchStatement(switchStatement, nameLowerer, rawIndentLevel),
        MatchStatement matchStatement => ToCMatchStatement(matchStatement, nameLowerer, rawIndentLevel),
        _ => ToRawCStatement(statement, nameLowerer, rawIndentLevel),
    };

    private static CStatementNode ToCMatchStatement(
        MatchStatement matchStatement,
        ImportedNameLowerer nameLowerer,
        int rawIndentLevel)
    {
        var matchExpression = ExpressionText(matchStatement.Expression);
        var matchInfo = nameLowerer.ResolveMatch(matchExpression);
        if (matchInfo is null)
        {
            return new CRawStatement(IndentLine($"/* unable to lower match {matchExpression} */", rawIndentLevel));
        }

        var cases = new List<CSwitchCase>();
        var defaultBody = new List<CStatementNode>();
        foreach (var arm in matchStatement.Arms)
        {
            var body = ToCMatchArmBody(arm, matchInfo, nameLowerer, rawIndentLevel);
            if (arm.Pattern == "_")
            {
                defaultBody.AddRange(body);
                continue;
            }

            cases.Add(new CSwitchCase(matchInfo.CaseLabel(arm.Pattern), body));
        }

        return new CSwitchStatement(ToCSimpleAccessExpression(matchInfo.TagAccess), cases, defaultBody);
    }

    private static IReadOnlyList<CStatementNode> ToCMatchArmBody(
        MatchArmNode arm,
        ImportedNameLowerer.MatchInfo matchInfo,
        ImportedNameLowerer matchLowerer,
        int rawIndentLevel)
    {
        var body = new List<CStatementNode>();
        var armLowerer = matchLowerer;
        if (arm.BindingName is not null && arm.Pattern != "_")
        {
            if (matchInfo.BindingType(arm.Pattern) is { } bindingType
                && matchInfo.BindingExpression(arm.Pattern) is { } bindingExpression)
            {
                body.Add(new CLocalDeclarationStatement(
                    LowerDeclaration(bindingType, arm.BindingName, matchLowerer.SelfType),
                    bindingExpression));
                armLowerer = matchLowerer.WithLocal(arm.BindingName, bindingType);
            }
        }

        body.AddRange(arm.Body.Select(statement => ToCStatement(statement, armLowerer, rawIndentLevel + 2)));
        if (!ArmAlwaysTransfers(arm.Body))
        {
            body.Add(new CBreakStatement());
        }

        return body;
    }

    private static CSwitchStatement ToCSwitchStatement(
        SwitchStatement switchStatement,
        ImportedNameLowerer nameLowerer,
        int rawIndentLevel) =>
        new(
            nameLowerer.LowerExpression(switchStatement.Expression),
            switchStatement.Cases
                .Select(switchCase => new CSwitchCase(
                    nameLowerer.Lower(switchCase.Pattern),
                    switchCase.Body
                        .Select(statement => ToCStatement(statement, nameLowerer, rawIndentLevel + 2))
                        .ToList()))
                .ToList(),
            switchStatement.DefaultBody
                .Select(statement => ToCStatement(statement, nameLowerer, rawIndentLevel + 2))
                .ToList());

    private static CForStatement ToCForStatement(
        ForStatement forStatement,
        ImportedNameLowerer nameLowerer,
        int rawIndentLevel) =>
        new(
            ToCForInitializer(forStatement.Initializer, nameLowerer),
            nameLowerer.LowerExpression(forStatement.Condition),
            nameLowerer.LowerExpression(forStatement.Increment),
            forStatement.Body
                .Select(statement => ToCStatement(statement, nameLowerer, rawIndentLevel + 1))
                .ToList());

    private static CForInitializerNode ToCForInitializer(
        ForInitializerNode initializer,
        ImportedNameLowerer nameLowerer) => initializer switch
    {
        ForDeclarationInitializerNode declaration => new CDeclarationForInitializer(
            $"{(declaration.IsConst ? "const " : "")}{LowerDeclaration(declaration.TypeNode, declaration.Type, declaration.Name, nameLowerer.SelfType)}",
            declaration.Initializer is null
                ? null
                : nameLowerer.LowerInitializerExpression(declaration.Type, declaration.Initializer)),
        ForExpressionInitializerNode expression => new CExpressionForInitializer(
            nameLowerer.LowerExpression(expression.Expression)),
        _ => new CEmptyForInitializer(),
    };

    private static CStatementNode ToCForeachStatement(
        ForeachStatement foreachStatement,
        ImportedNameLowerer nameLowerer,
        int rawIndentLevel)
    {
        var iterableExpression = ExpressionText(foreachStatement.IterableExpression);

        if (foreachStatement.IterableExpression is ScalarRangeExpressionNode rangeExpression)
        {
            return ToCScalarRangeForeachStatement(foreachStatement, rangeExpression, nameLowerer, rawIndentLevel);
        }

        if (foreachStatement.KeyBinding is not null)
        {
            return nameLowerer.ResolveIteratorIterable(foreachStatement.IterableExpression, keyValue: true) is { } keyValueIterable
                ? ToCIteratorForeachStatement(foreachStatement, keyValueIterable, nameLowerer, rawIndentLevel)
                : new CRawStatement(IndentLine($"/* key/value foreach requires KeyValueIterable for {iterableExpression} */", rawIndentLevel));
        }

        if (nameLowerer.ResolveIteratorIterable(foreachStatement.IterableExpression, keyValue: false) is { } iteratorIterable)
        {
            return ToCIteratorForeachStatement(foreachStatement, iteratorIterable, nameLowerer, rawIndentLevel);
        }

        var iterable = nameLowerer.ResolveContiguousIterable(iterableExpression);
        if (iterable is null)
        {
            return new CRawStatement(IndentLine($"/* unable to lower foreach {foreachStatement.ItemName} in {iterableExpression} */", rawIndentLevel));
        }

        var indexName = foreachStatement.IndexBinding?.Name ?? $"__cx_i{rawIndentLevel}";
        var loopIndexName = foreachStatement.IndexBinding is null
            ? indexName
            : $"__cx_i{rawIndentLevel}";
        var itemType = GetForeachValueType(foreachStatement, iterable.ElementType);
        var itemStorageType = GetForeachValueStorageType(foreachStatement, iterable.ElementType);
        var bodyLowerer = nameLowerer.WithImplicitReferenceLocal(
            foreachStatement.ItemName,
            itemType,
            itemStorageType,
            foreachStatement.ValueBinding.IsConst);
        if (foreachStatement.IndexBinding is { } indexBinding)
        {
            var indexType = string.IsNullOrWhiteSpace(indexBinding.Type) ? "usize" : indexBinding.Type;
            bodyLowerer = bodyLowerer.WithLocal(indexBinding.Name, indexType);
        }

        var body = new List<CStatementNode>
        {
        };

        if (foreachStatement.IndexBinding is not null)
        {
            body.Add(new CLocalDeclarationStatement(
                "const " + LowerDeclaration(GetForeachIndexType(foreachStatement), indexName),
                new CNameExpression(loopIndexName)));
        }

        body.Add(new CLocalDeclarationStatement(
            LowerDeclaration(itemStorageType, foreachStatement.ItemName, nameLowerer.SelfType),
            new CUnaryExpression(
                "&",
                new CIndexExpression(ToCSimpleAccessExpression(iterable.DataAccess), new CNameExpression(loopIndexName)))));

        body.AddRange(foreachStatement.Body.Select(statement => ToCStatement(statement, bodyLowerer, rawIndentLevel + 1)));

        return new CForStatement(
            new CDeclarationForInitializer(
                LowerDeclaration(GetForeachIndexType(foreachStatement), loopIndexName),
                new CLiteralExpression("0")),
            new CBinaryExpression(
                new CNameExpression(loopIndexName),
                "<",
                ToCSimpleAccessExpression(iterable.LengthAccess)),
            new CPostfixExpression(new CNameExpression(loopIndexName), "++"),
            body);
    }

    private static CStatementNode ToCIteratorForeachStatement(
        ForeachStatement foreachStatement,
        IteratorIterableInfo iteratorIterable,
        ImportedNameLowerer nameLowerer,
        int rawIndentLevel)
    {
        var iteratorName = $"__cx_it{rawIndentLevel}";
        var indexName = foreachStatement.IndexBinding?.Name ?? $"__cx_i{rawIndentLevel}";
        var loopIndexName = foreachStatement.IndexBinding is null
            ? indexName
            : $"__cx_i{rawIndentLevel}";
        var indexType = GetForeachIndexType(foreachStatement);
        var iteratorLowerer = nameLowerer.WithLocal(iteratorName, iteratorIterable.IteratorType);
        var itemType = GetForeachValueType(foreachStatement, iteratorIterable.ValueType);
        var itemStorageType = GetForeachValueStorageType(foreachStatement, iteratorIterable.ValueType);
        var bodyLowerer = iteratorLowerer.WithImplicitReferenceLocal(
            foreachStatement.ItemName,
            itemType,
            itemStorageType,
            foreachStatement.ValueBinding.IsConst);

        if (foreachStatement.KeyBinding is { } keyBinding && iteratorIterable.KeyType is { } keyElementType)
        {
            var keyType = GetForeachBindingValueType(keyBinding, keyElementType);
            var keyStorageType = GetForeachBindingStorageType(keyBinding, keyElementType, forceConst: true);
            bodyLowerer = bodyLowerer.WithImplicitReferenceLocal(keyBinding.Name, keyType, keyStorageType, isConst: true);
        }

        if (foreachStatement.IndexBinding is { } indexBinding)
        {
            bodyLowerer = bodyLowerer.WithLocal(indexBinding.Name, indexType);
        }

        var block = new List<CStatementNode>
        {
            new CLocalDeclarationStatement(
                LowerDeclaration(iteratorIterable.IteratorType, iteratorName, nameLowerer.SelfType),
                iteratorIterable.IteratorInitializer),
        };

        if (foreachStatement.IndexBinding is not null)
        {
            block.Add(new CLocalDeclarationStatement(
                LowerDeclaration(indexType, loopIndexName),
                new CLiteralExpression("0")));
        }

        var whileBody = new List<CStatementNode>();
        if (foreachStatement.IndexBinding is not null)
        {
            whileBody.Add(new CLocalDeclarationStatement(
                "const " + LowerDeclaration(indexType, indexName),
                new CNameExpression(loopIndexName)));
        }

        if (foreachStatement.KeyBinding is { } key && iteratorIterable.KeyType is { } keyIteratorElementType)
        {
            whileBody.Add(new CLocalDeclarationStatement(
                LowerDeclaration(GetForeachBindingStorageType(key, keyIteratorElementType, forceConst: true), key.Name, nameLowerer.SelfType),
                iteratorLowerer.LowerExpression(SyntheticMemberCall(foreachStatement.Location, iteratorName, "key"))));
        }

        whileBody.Add(new CLocalDeclarationStatement(
            LowerDeclaration(itemStorageType, foreachStatement.ItemName, nameLowerer.SelfType),
            iteratorLowerer.LowerExpression(SyntheticMemberCall(foreachStatement.Location, iteratorName, "value"))));

        whileBody.AddRange(foreachStatement.Body.Select(statement => ToCStatement(statement, bodyLowerer, rawIndentLevel + 1)));
        if (foreachStatement.IndexBinding is not null)
        {
            whileBody.Add(new CExpressionStatement(new CPostfixExpression(new CNameExpression(loopIndexName), "++")));
        }

        block.Add(new CWhileStatement(
            iteratorLowerer.LowerExpression(SyntheticMemberCall(foreachStatement.Location, iteratorName, "next")),
            whileBody));

        return new CBlockStatement(block);
    }

    private static CStatementNode ToCScalarRangeForeachStatement(
        ForeachStatement foreachStatement,
        ScalarRangeExpressionNode rangeExpression,
        ImportedNameLowerer nameLowerer,
        int rawIndentLevel)
    {
        if (foreachStatement.KeyBinding is not null)
        {
            return new CRawStatement(IndentLine("/* key/value foreach is not supported for scalar ranges */", rawIndentLevel));
        }

        var itemType = GetForeachValueType(foreachStatement, InferScalarRangeType(rangeExpression, nameLowerer));
        var itemName = foreachStatement.ItemName;
        var bodyLowerer = nameLowerer.WithLocal(itemName, itemType);
        var indexName = foreachStatement.IndexBinding?.Name ?? $"__cx_i{rawIndentLevel}";
        var loopIndexName = foreachStatement.IndexBinding is null
            ? indexName
            : $"__cx_i{rawIndentLevel}";
        if (foreachStatement.IndexBinding is { } indexBinding)
        {
            bodyLowerer = bodyLowerer.WithLocal(indexBinding.Name, GetForeachIndexType(foreachStatement));
        }

        var body = new List<CStatementNode>();
        if (foreachStatement.IndexBinding is not null)
        {
            body.Add(new CLocalDeclarationStatement(
                "const " + LowerDeclaration(GetForeachIndexType(foreachStatement), indexName),
                new CNameExpression(loopIndexName)));
        }

        body.AddRange(foreachStatement.Body.Select(statement => ToCStatement(statement, bodyLowerer, rawIndentLevel + 1)));

        var conditionOperator = rangeExpression.IsInclusive ? "<=" : "<";
        var forStatement = new CForStatement(
            new CDeclarationForInitializer(
                LowerDeclaration(itemType, itemName, nameLowerer.SelfType),
                nameLowerer.LowerExpression(rangeExpression.Start)),
            new CBinaryExpression(
                new CNameExpression(itemName),
                conditionOperator,
                nameLowerer.LowerExpression(rangeExpression.End)),
            new CPostfixExpression(new CNameExpression(itemName), "++"),
            body);

        if (foreachStatement.IndexBinding is null)
        {
            return forStatement;
        }

        return new CBlockStatement(
        [
            new CLocalDeclarationStatement(
                LowerDeclaration(GetForeachIndexType(foreachStatement), loopIndexName),
                new CLiteralExpression("0")),
            forStatement with
            {
                Body = body.Concat([new CExpressionStatement(new CPostfixExpression(new CNameExpression(loopIndexName), "++"))]).ToList(),
            },
        ]);
    }

    private static string InferScalarRangeType(ScalarRangeExpressionNode rangeExpression, ImportedNameLowerer nameLowerer)
    {
        var startType = nameLowerer.ResolveExpressionTypeForLowering(rangeExpression.Start);
        var endType = nameLowerer.ResolveExpressionTypeForLowering(rangeExpression.End);
        if (string.Equals(startType, endType, StringComparison.Ordinal))
        {
            return startType ?? "int";
        }

        if (startType == "int" && IsIntegerLiteral(rangeExpression.Start) && endType is not null)
        {
            return endType;
        }

        if (endType == "int" && IsIntegerLiteral(rangeExpression.End) && startType is not null)
        {
            return startType;
        }

        return startType ?? endType ?? "int";
    }

    private static bool IsIntegerLiteral(ExpressionNode expression) =>
        expression is LiteralExpressionNode literal
        && Regex.IsMatch(literal.SourceText.Trim(), @"^-?\d+$");

    private static string GetForeachIndexType(ForeachStatement foreachStatement) =>
        foreachStatement.IndexBinding is { } indexBinding && !string.IsNullOrWhiteSpace(indexBinding.Type)
            ? indexBinding.Type
            : "usize";

    private static string GetForeachValueType(ForeachStatement foreachStatement, string elementType) =>
        string.IsNullOrWhiteSpace(foreachStatement.ValueBinding.Type)
            ? elementType
            : foreachStatement.ValueBinding.Type;

    private static string GetForeachBindingValueType(ForeachBinding binding, string elementType) =>
        string.IsNullOrWhiteSpace(binding.Type)
            ? elementType
            : binding.Type;

    private static string GetForeachValueStorageType(ForeachStatement foreachStatement, string elementType)
        => GetForeachBindingStorageType(foreachStatement.ValueBinding, elementType, forceConst: false);

    private static string GetForeachBindingStorageType(ForeachBinding binding, string elementType, bool forceConst)
    {
        var declaredType = GetForeachBindingValueType(binding, elementType);
        if ((forceConst || binding.IsConst)
            && !declaredType.TrimStart().StartsWith("const ", StringComparison.Ordinal))
        {
            declaredType = "const " + declaredType;
        }

        return declaredType.TrimEnd().EndsWith("*", StringComparison.Ordinal)
            ? declaredType
            : declaredType + "*";
    }

    private static CallExpressionNode SyntheticMemberCall(
        Cx.Compiler.Syntax.Location location,
        string targetName,
        string methodName)
    {
        var target = new NameExpressionNode(location, targetName);
        var callee = new MemberExpressionNode(location, targetName + "." + methodName, target, methodName);
        return new CallExpressionNode(location, targetName + "." + methodName + "()", callee, []);
    }

    private static CExpression ToCSimpleAccessExpression(string expression)
    {
        expression = expression.Trim();
        if (Regex.IsMatch(expression, @"^\d+$"))
        {
            return new CLiteralExpression(expression);
        }

        var tokens = Regex.Matches(expression, @"(->|\.|[A-Za-z_][A-Za-z0-9_]*)")
            .Select(match => match.Value)
            .ToList();
        if (tokens.Count == 0 || tokens[0] is "." or "->")
        {
            return new CRawExpression(expression);
        }

        CExpression result = new CNameExpression(tokens[0]);
        for (var i = 1; i < tokens.Count; i += 2)
        {
            if (i + 1 >= tokens.Count || tokens[i] is not ("." or "->"))
            {
                return new CRawExpression(expression);
            }

            result = new CMemberExpression(result, tokens[i], tokens[i + 1]);
        }

        return result;
    }

    private static CWhileStatement ToCWhileStatement(
        WhileStatement whileStatement,
        ImportedNameLowerer nameLowerer,
        int rawIndentLevel) =>
        new(
            nameLowerer.LowerExpression(whileStatement.Condition),
            whileStatement.Body
                .Select(statement => ToCStatement(statement, nameLowerer, rawIndentLevel + 1))
                .ToList());

    private static CIfStatement ToCIfStatement(
        IfStatement ifStatement,
        ImportedNameLowerer nameLowerer,
        int rawIndentLevel) =>
        new(
            nameLowerer.LowerExpression(ifStatement.Condition),
            ifStatement.ThenBody
                .Select(statement => ToCStatement(statement, nameLowerer, rawIndentLevel + 1))
                .ToList(),
            ToCElseClause(ifStatement.ElseBranch, nameLowerer, rawIndentLevel));

    private static CElseClause? ToCElseClause(
        StatementNode? elseBranch,
        ImportedNameLowerer nameLowerer,
        int rawIndentLevel) => elseBranch switch
    {
        null => null,
        IfStatement elseIf => new CElseIfClause(ToCIfStatement(elseIf, nameLowerer, rawIndentLevel)),
        ElseBlockStatement elseBlock => new CElseBlockClause(
            elseBlock.Body
                .Select(statement => ToCStatement(statement, nameLowerer, rawIndentLevel + 1))
                .ToList()),
        _ => new CElseBlockClause([ToRawCStatement(elseBranch, nameLowerer, rawIndentLevel + 1)]),
    };

    private static CLocalDeclarationStatement ToCLocalDeclaration(
        LetStatement let,
        ImportedNameLowerer nameLowerer)
    {
        var declaration = (let.IsConst ? "const " : "")
            + LowerDeclaration(let.TypeNode, let.Type, let.Name, nameLowerer.SelfType);
        var initializer = let.Initializer is null
            ? null
            : nameLowerer.LowerInitializerExpression(let.Type, let.Initializer);
        return new CLocalDeclarationStatement(declaration, initializer);
    }

    private static CRawStatement ToRawCStatement(
        StatementNode statement,
        ImportedNameLowerer nameLowerer,
        int rawIndentLevel)
    {
        var body = new StringBuilder();
        EmitStatement(body, statement, nameLowerer, rawIndentLevel);
        return new CRawStatement(body.ToString());
    }

    private static CFunctionDeclaration ToCFunctionDeclaration(ExternFunctionNode function) =>
        new(
            LowerType(function.ReturnTypeNode, function.ReturnType),
            function.Name,
            function.Parameters
                .Select(parameter => LowerParameterDeclaration(parameter))
                .ToList());

    private static CGlobalDeclaration ToCGlobalDeclaration(
        GlobalVariableNode global,
        ImportedNameLowerer nameLowerer)
    {
        var declaration = (global.IsConst ? "const " : "") + LowerDeclaration(global.TypeNode, global.Type, global.Name);
        var initializer = global.Initializer is null
            ? null
            : nameLowerer.LowerInitializerExpression(global.Type, global.Initializer);
        return new CGlobalDeclaration(declaration, initializer);
    }

    private static IEnumerable<CDeclareNode> GetCDeclarationsToInclude(ProgramNode program)
    {
        var usage = GetCDeclarationUsage(program);
        foreach (var declaration in program.CDeclarations)
        {
            if (IsCDeclarationUsed(declaration, usage))
            {
                yield return declaration;
            }
        }
    }

    private sealed record CDeclarationUsage(
        IReadOnlySet<string> Functions,
        IReadOnlySet<string> Types,
        IReadOnlySet<string> Values);

    private static CDeclarationUsage GetCDeclarationUsage(ProgramNode program)
    {
        var declaredFunctions = program.CDeclarations
            .SelectMany(declaration => declaration.Functions)
            .Select(function => function.Name)
            .Where(name => !string.IsNullOrWhiteSpace(name))
            .ToHashSet(StringComparer.Ordinal);
        var declaredTypes = program.CDeclarations
            .SelectMany(GetCDeclarationTypeNames)
            .Where(name => !string.IsNullOrWhiteSpace(name))
            .ToHashSet(StringComparer.Ordinal);
        var declaredValues = program.CDeclarations
            .SelectMany(GetCDeclarationValueNames)
            .Where(name => !string.IsNullOrWhiteSpace(name))
            .ToHashSet(StringComparer.Ordinal);

        var functions = EnumerateExpressionNodes(program)
            .Select(GetCalledFunctionName)
            .Where(name => name is not null && declaredFunctions.Contains(name))
            .Select(name => name!)
            .ToHashSet(StringComparer.Ordinal);
        var types = EnumerateTypeReferences(program)
            .SelectMany(ExtractTypeNames)
            .Where(declaredTypes.Contains)
            .ToHashSet(StringComparer.Ordinal);
        var values = EnumerateExpressionNodes(program)
            .Select(GetValueReferenceName)
            .Where(name => name is not null && declaredValues.Contains(name))
            .Select(name => name!)
            .ToHashSet(StringComparer.Ordinal);

        return new CDeclarationUsage(functions, types, values);
    }

    private static string? GetCalledFunctionName(ExpressionNode expression) => expression switch
    {
        CallExpressionNode call => GetQualifiedName(call.Callee),
        GenericCallExpressionNode call => GetQualifiedName(call.Callee),
        _ => null,
    };

    private static string? GetValueReferenceName(ExpressionNode expression) => expression switch
    {
        NameExpressionNode name => name.SourceText,
        MemberExpressionNode member => GetQualifiedName(member),
        _ => null,
    };

    private static IEnumerable<string> GetCDeclarationTypeNames(CDeclareNode declaration) =>
        declaration.TypeAliases.Select(typeAlias => typeAlias.Name)
            .Concat(declaration.Structs.Select(structNode => structNode.Name))
            .Concat(declaration.Enums.Select(enumNode => enumNode.Name))
            .Concat(declaration.Unions.Select(union => union.Name));

    private static IEnumerable<string> GetCDeclarationValueNames(CDeclareNode declaration) =>
        declaration.Constants.Select(constant => constant.Name)
            .Concat(declaration.Enums.SelectMany(enumNode => enumNode.Members.Select(member => member.Name)));

    private static IEnumerable<ExpressionNode> EnumerateExpressionNodes(ProgramNode program)
    {
        foreach (var global in program.GlobalVariables.Where(global => !global.IsHeaderDeclaration))
        {
            if (global.Initializer is null)
            {
                continue;
            }

            foreach (var expression in EnumerateExpressionNodes(global.Initializer))
            {
                yield return expression;
            }
        }

        foreach (var function in program.Functions)
        {
            foreach (var expression in EnumerateExpressionNodes(function.Body))
            {
                yield return expression;
            }
        }
    }

    private static IEnumerable<string> EnumerateTypeReferences(ProgramNode program)
    {
        foreach (var global in program.GlobalVariables.Where(global => !global.IsHeaderDeclaration))
        {
            yield return global.Type;
            foreach (var type in EnumerateExpressionTypeReferences(global.Initializer))
            {
                yield return type;
            }
        }

        foreach (var typeAlias in program.TypeAliases.Where(typeAlias => !typeAlias.IsHeaderDeclaration))
        {
            yield return typeAlias.TargetType;
        }

        foreach (var function in program.Functions)
        {
            yield return function.ReturnType;
            foreach (var parameter in function.Parameters.Where(parameter => !parameter.IsVariadic))
            {
                yield return parameter.Type;
            }

            foreach (var type in EnumerateStatementTypeReferences(function.Body))
            {
                yield return type;
            }
        }

        foreach (var structNode in program.Structs.Where(structNode => !structNode.IsHeaderDeclaration))
        {
            foreach (var field in structNode.Fields)
            {
                yield return field.Type;
            }
        }

        foreach (var taggedUnion in program.TaggedUnions.Where(taggedUnion => !taggedUnion.IsHeaderDeclaration))
        {
            foreach (var variant in taggedUnion.Variants)
            {
                yield return variant.Type;
            }
        }
    }

    private static IEnumerable<string> EnumerateStatementTypeReferences(IEnumerable<StatementNode> statements)
    {
        foreach (var statement in statements)
        {
            switch (statement)
            {
                case LetStatement letStatement:
                    yield return letStatement.Type;
                    foreach (var type in EnumerateExpressionTypeReferences(letStatement.Initializer))
                    {
                        yield return type;
                    }
                    break;
                case ReturnStatement returnStatement:
                    foreach (var type in EnumerateExpressionTypeReferences(returnStatement.Expression))
                    {
                        yield return type;
                    }
                    break;
                case CStatement cStatement:
                    foreach (var type in EnumerateExpressionTypeReferences(cStatement.Expression))
                    {
                        yield return type;
                    }
                    break;
                case IfStatement ifStatement:
                    foreach (var type in EnumerateExpressionTypeReferences(ifStatement.Condition)
                        .Concat(EnumerateStatementTypeReferences(ifStatement.ThenBody)))
                    {
                        yield return type;
                    }
                    if (ifStatement.ElseBranch is not null)
                    {
                        foreach (var type in EnumerateStatementTypeReferences([ifStatement.ElseBranch]))
                        {
                            yield return type;
                        }
                    }
                    break;
                case ElseBlockStatement elseBlock:
                    foreach (var type in EnumerateStatementTypeReferences(elseBlock.Body))
                    {
                        yield return type;
                    }
                    break;
                case WhileStatement whileStatement:
                    foreach (var type in EnumerateExpressionTypeReferences(whileStatement.Condition)
                        .Concat(EnumerateStatementTypeReferences(whileStatement.Body)))
                    {
                        yield return type;
                    }
                    break;
                case ForStatement forStatement:
                    foreach (var type in EnumerateForInitializerTypeReferences(forStatement.Initializer)
                        .Concat(EnumerateExpressionTypeReferences(forStatement.Condition))
                        .Concat(EnumerateExpressionTypeReferences(forStatement.Increment))
                        .Concat(EnumerateStatementTypeReferences(forStatement.Body)))
                    {
                        yield return type;
                    }
                    break;
                case ForeachStatement foreachStatement:
                    foreach (var type in EnumerateExpressionTypeReferences(foreachStatement.IterableExpression)
                        .Concat(EnumerateForeachBindingTypeReferences(foreachStatement))
                        .Concat(EnumerateStatementTypeReferences(foreachStatement.Body)))
                    {
                        yield return type;
                    }
                    break;
                case SwitchStatement switchStatement:
                    foreach (var type in EnumerateExpressionTypeReferences(switchStatement.Expression))
                    {
                        yield return type;
                    }
                    foreach (var switchCase in switchStatement.Cases)
                    {
                        foreach (var type in EnumerateExpressionTypeReferences(switchCase.Pattern)
                            .Concat(EnumerateStatementTypeReferences(switchCase.Body)))
                        {
                            yield return type;
                        }
                    }
                    foreach (var type in EnumerateStatementTypeReferences(switchStatement.DefaultBody))
                    {
                        yield return type;
                    }
                    break;
                case MatchStatement matchStatement:
                    foreach (var type in EnumerateExpressionTypeReferences(matchStatement.Expression))
                    {
                        yield return type;
                    }
                    foreach (var arm in matchStatement.Arms)
                    {
                        foreach (var type in EnumerateStatementTypeReferences(arm.Body))
                        {
                            yield return type;
                        }
                    }
                    break;
            }
        }
    }

    private static IEnumerable<string> EnumerateForInitializerTypeReferences(ForInitializerNode initializer) => initializer switch
    {
        ForDeclarationInitializerNode declaration => [declaration.Type, .. EnumerateExpressionTypeReferences(declaration.Initializer)],
        ForExpressionInitializerNode expression => EnumerateExpressionTypeReferences(expression.Expression),
        _ => [],
    };

    private static IEnumerable<string> EnumerateExpressionTypeReferences(ExpressionNode? expression)
    {
        if (expression is null)
        {
            yield break;
        }

        foreach (var node in EnumerateExpressionNodes(expression))
        {
            switch (node)
            {
                case CastExpressionNode cast:
                    yield return cast.TargetType;
                    break;
                case InitializerExpressionNode { TypeName: not null } initializer:
                    yield return initializer.TypeName;
                    break;
                case SizeOfExpressionNode { TypeOperand: not null } sizeOf:
                    yield return sizeOf.TypeOperand;
                    break;
            }
        }
    }

    private static IEnumerable<string> EnumerateForeachBindingTypeReferences(ForeachStatement foreachStatement)
    {
        foreach (var binding in new[] { foreachStatement.IndexBinding, foreachStatement.KeyBinding, foreachStatement.ValueBinding })
        {
            if (!string.IsNullOrWhiteSpace(binding?.Type))
            {
                yield return binding.Type;
            }
        }
    }

    private static IEnumerable<string> ExtractTypeNames(string type)
    {
        type = NormalizeTypeReferenceText(type);
        foreach (Match match in Regex.Matches(type, @"[A-Za-z_][A-Za-z0-9_]*(?:\.[A-Za-z_][A-Za-z0-9_]*)?"))
        {
            var name = match.Value;
            if (!IsTypeReferenceKeyword(name))
            {
                yield return name;
            }
        }
    }

    private static string NormalizeTypeReferenceText(string type) =>
        type
            .Replace("*", " ", StringComparison.Ordinal)
            .Replace("[", " ", StringComparison.Ordinal)
            .Replace("]", " ", StringComparison.Ordinal)
            .Replace("(", " ", StringComparison.Ordinal)
            .Replace(")", " ", StringComparison.Ordinal)
            .Replace(",", " ", StringComparison.Ordinal)
            .Replace("->", " ", StringComparison.Ordinal)
            .Replace("<", " ", StringComparison.Ordinal)
            .Replace(">", " ", StringComparison.Ordinal);

    private static bool IsTypeReferenceKeyword(string name) =>
        name is
            "const" or
            "fn" or
            "opaque" or
            "void" or
            "char" or
            "short" or
            "int" or
            "long" or
            "float" or
            "double" or
            "bool" or
            "usize" or
            "i8" or
            "i16" or
            "i32" or
            "i64" or
            "u8" or
            "u16" or
            "u32" or
            "u64";

    private static bool IsCDeclarationUsed(CDeclareNode declaration, CDeclarationUsage usage) =>
        declaration.Functions.Any(function => usage.Functions.Contains(function.Name))
        || GetCDeclarationTypeNames(declaration).Any(usage.Types.Contains)
        || GetCDeclarationValueNames(declaration).Any(usage.Values.Contains);

    private static void EmitStruct(StringBuilder builder, StructNode structNode)
    {
        builder.AppendLine($"typedef struct {structNode.Name}");
        builder.AppendLine("{");

        foreach (var field in structNode.Fields)
        {
            builder.AppendLine($"    {LowerStructFieldDeclaration(structNode, field)};");
        }

        builder.AppendLine($"}} {structNode.Name};");
    }

    private static string LowerStructFieldDeclaration(StructNode structNode, StructFieldNode field)
    {
        var selfPointerType = structNode.Name + "*";
        return field.Type == selfPointerType
            ? $"struct {structNode.Name}* {field.Name}"
            : LowerDeclaration(field.TypeNode, field.Type, field.Name);
    }

    private static void EmitEnum(StringBuilder builder, EnumNode enumNode)
    {
        builder.AppendLine("typedef enum");
        builder.AppendLine("{");

        for (var i = 0; i < enumNode.Members.Count; i++)
        {
            var member = enumNode.Members[i];
            var value = string.IsNullOrWhiteSpace(member.Value) ? "" : " = " + member.Value;
            var comma = i == enumNode.Members.Count - 1 ? "" : ",";
            builder.AppendLine($"    {member.Name}{value}{comma}");
        }

        builder.AppendLine($"}} {enumNode.Name};");
    }

    private static IReadOnlyList<FunctionNode> GetFunctionsToEmit(ProgramNode program)
    =>
        program.Functions
            .Where(function => function.TypeParameters.Count == 0)
            .ToList();

    private static IEnumerable<ExpressionNode> EnumerateExpressionNodes(IEnumerable<StatementNode> statements)
    {
        foreach (var statement in statements)
        {
            switch (statement)
            {
                case LetStatement { Initializer: not null } let:
                    foreach (var expression in EnumerateExpressionNodes(let.Initializer))
                    {
                        yield return expression;
                    }
                    break;
                case ReturnStatement ret:
                    foreach (var expression in EnumerateExpressionNodes(ret.Expression))
                    {
                        yield return expression;
                    }
                    break;
                case CStatement c:
                    foreach (var expression in EnumerateExpressionNodes(c.Expression))
                    {
                        yield return expression;
                    }
                    break;
                case IfStatement ifStatement:
                    foreach (var expression in EnumerateExpressionNodes(ifStatement.Condition))
                    {
                        yield return expression;
                    }

                    foreach (var expression in EnumerateExpressionNodes(ifStatement.ThenBody))
                    {
                        yield return expression;
                    }

                    if (ifStatement.ElseBranch is not null)
                    {
                        foreach (var expression in EnumerateExpressionNodes([ifStatement.ElseBranch]))
                        {
                            yield return expression;
                        }
                    }
                    break;
                case ElseBlockStatement elseBlock:
                    foreach (var expression in EnumerateExpressionNodes(elseBlock.Body))
                    {
                        yield return expression;
                    }
                    break;
                case WhileStatement whileStatement:
                    foreach (var expression in EnumerateExpressionNodes(whileStatement.Condition))
                    {
                        yield return expression;
                    }

                    foreach (var expression in EnumerateExpressionNodes(whileStatement.Body))
                    {
                        yield return expression;
                    }
                    break;
                case ForStatement forStatement:
                    if (forStatement.Initializer is ForDeclarationInitializerNode { Initializer: not null } declaration)
                    {
                        foreach (var expression in EnumerateExpressionNodes(declaration.Initializer))
                        {
                            yield return expression;
                        }
                    }
                    else if (forStatement.Initializer is ForExpressionInitializerNode initializer)
                    {
                        foreach (var expression in EnumerateExpressionNodes(initializer.Expression))
                        {
                            yield return expression;
                        }
                    }

                    foreach (var expression in EnumerateExpressionNodes(forStatement.Condition))
                    {
                        yield return expression;
                    }

                    foreach (var expression in EnumerateExpressionNodes(forStatement.Increment))
                    {
                        yield return expression;
                    }

                    foreach (var expression in EnumerateExpressionNodes(forStatement.Body))
                    {
                        yield return expression;
                    }
                    break;
                case ForeachStatement foreachStatement:
                    foreach (var expression in EnumerateExpressionNodes(foreachStatement.IterableExpression))
                    {
                        yield return expression;
                    }

                    foreach (var expression in EnumerateExpressionNodes(foreachStatement.Body))
                    {
                        yield return expression;
                    }
                    break;
                case SwitchStatement switchStatement:
                    foreach (var expression in EnumerateExpressionNodes(switchStatement.Expression))
                    {
                        yield return expression;
                    }

                    foreach (var switchCase in switchStatement.Cases)
                    {
                        foreach (var expression in EnumerateExpressionNodes(switchCase.Pattern))
                        {
                            yield return expression;
                        }

                        foreach (var expression in EnumerateExpressionNodes(switchCase.Body))
                        {
                            yield return expression;
                        }
                    }

                    foreach (var expression in EnumerateExpressionNodes(switchStatement.DefaultBody))
                    {
                        yield return expression;
                    }
                    break;
                case MatchStatement matchStatement:
                    foreach (var expression in EnumerateExpressionNodes(matchStatement.Expression))
                    {
                        yield return expression;
                    }

                    foreach (var arm in matchStatement.Arms)
                    {
                        foreach (var expression in EnumerateExpressionNodes(arm.Body))
                        {
                            yield return expression;
                        }
                    }
                    break;
            }
        }
    }

    private static IEnumerable<ExpressionNode> EnumerateExpressionNodes(ExpressionNode expression)
    {
        yield return expression;
        switch (expression)
        {
            case ParenthesizedExpressionNode parenthesized:
                foreach (var child in EnumerateExpressionNodes(parenthesized.Expression)) yield return child;
                break;
            case CastExpressionNode cast:
                foreach (var child in EnumerateExpressionNodes(cast.Expression)) yield return child;
                break;
            case UnaryExpressionNode unary:
                foreach (var child in EnumerateExpressionNodes(unary.Operand)) yield return child;
                break;
            case PostfixExpressionNode postfix:
                foreach (var child in EnumerateExpressionNodes(postfix.Operand)) yield return child;
                break;
            case SizeOfExpressionNode { ExpressionOperand: not null } sizeOf:
                foreach (var child in EnumerateExpressionNodes(sizeOf.ExpressionOperand)) yield return child;
                break;
            case BinaryExpressionNode binary:
                foreach (var child in EnumerateExpressionNodes(binary.Left)) yield return child;
                foreach (var child in EnumerateExpressionNodes(binary.Right)) yield return child;
                break;
            case ScalarRangeExpressionNode range:
                foreach (var child in EnumerateExpressionNodes(range.Start)) yield return child;
                foreach (var child in EnumerateExpressionNodes(range.End)) yield return child;
                break;
            case ConditionalExpressionNode conditional:
                foreach (var child in EnumerateExpressionNodes(conditional.Condition)) yield return child;
                foreach (var child in EnumerateExpressionNodes(conditional.WhenTrue)) yield return child;
                foreach (var child in EnumerateExpressionNodes(conditional.WhenFalse)) yield return child;
                break;
            case InitializerExpressionNode initializer:
                foreach (var field in initializer.Fields)
                {
                    foreach (var child in EnumerateExpressionNodes(field.Value)) yield return child;
                }

                foreach (var value in initializer.Values)
                {
                    foreach (var child in EnumerateExpressionNodes(value)) yield return child;
                }
                break;
            case FunctionExpressionNode function:
                if (function.ExpressionBody is not null)
                {
                    foreach (var child in EnumerateExpressionNodes(function.ExpressionBody)) yield return child;
                }

                if (function.BlockBody is not null)
                {
                    foreach (var child in EnumerateExpressionNodes(function.BlockBody)) yield return child;
                }
                break;
            case AssignmentExpressionNode assignment:
                foreach (var child in EnumerateExpressionNodes(assignment.Target)) yield return child;
                foreach (var child in EnumerateExpressionNodes(assignment.Value)) yield return child;
                break;
            case CallExpressionNode call:
                foreach (var child in EnumerateExpressionNodes(call.Callee)) yield return child;
                foreach (var argument in call.Arguments)
                {
                    foreach (var child in EnumerateExpressionNodes(argument)) yield return child;
                }
                break;
            case GenericCallExpressionNode call:
                foreach (var child in EnumerateExpressionNodes(call.Callee)) yield return child;
                foreach (var argument in call.Arguments)
                {
                    foreach (var child in EnumerateExpressionNodes(argument)) yield return child;
                }
                break;
            case MemberExpressionNode member:
                foreach (var child in EnumerateExpressionNodes(member.Target)) yield return child;
                break;
            case IndexExpressionNode index:
                foreach (var child in EnumerateExpressionNodes(index.Target)) yield return child;
                foreach (var child in EnumerateExpressionNodes(index.Index)) yield return child;
                break;
        }
    }

    private static IEnumerable<(string Name, string Type)> CollectLocalVariables(IEnumerable<StatementNode> statements)
    {
        foreach (var statement in statements)
        {
            switch (statement)
            {
                case LetStatement let:
                    yield return (let.Name, let.Type);
                    break;
                case IfStatement ifStatement:
                    foreach (var variable in CollectLocalVariables(ifStatement.ThenBody))
                    {
                        yield return variable;
                    }

                    if (ifStatement.ElseBranch is not null)
                    {
                        foreach (var variable in CollectLocalVariables([ifStatement.ElseBranch]))
                        {
                            yield return variable;
                        }
                    }

                    break;
                case ElseBlockStatement elseBlock:
                    foreach (var variable in CollectLocalVariables(elseBlock.Body))
                    {
                        yield return variable;
                    }
                    break;
                case WhileStatement whileStatement:
                    foreach (var variable in CollectLocalVariables(whileStatement.Body))
                    {
                        yield return variable;
                    }
                    break;
                case ForStatement forStatement:
                    if (forStatement.Initializer is ForDeclarationInitializerNode declaration)
                    {
                        yield return (declaration.Name, declaration.Type);
                    }

                    foreach (var variable in CollectLocalVariables(forStatement.Body))
                    {
                        yield return variable;
                    }
                    break;
                case ForeachStatement foreachStatement:
                    foreach (var variable in CollectLocalVariables(foreachStatement.Body))
                    {
                        yield return variable;
                    }
                    break;
                case SwitchStatement switchStatement:
                    foreach (var switchCase in switchStatement.Cases)
                    {
                        foreach (var variable in CollectLocalVariables(switchCase.Body))
                        {
                            yield return variable;
                        }
                    }

                    foreach (var variable in CollectLocalVariables(switchStatement.DefaultBody))
                    {
                        yield return variable;
                    }
                    break;
                case MatchStatement matchStatement:
                    foreach (var arm in matchStatement.Arms)
                    {
                        foreach (var variable in CollectLocalVariables(arm.Body))
                        {
                            yield return variable;
                        }
                    }
                    break;
            }
        }
    }

    private static string SubstituteAdapterBaseType(TypeAdapterNode adapter, IReadOnlyList<string> receiverArguments)
    {
        if (adapter.TypeParameters.Count == 0 || adapter.TypeParameters.Count != receiverArguments.Count)
        {
            return adapter.BaseType;
        }

        var substitutions = adapter.TypeParameters
            .Zip(receiverArguments)
            .ToDictionary(pair => pair.First, pair => pair.Second, StringComparer.Ordinal);
        return SubstituteGenericType(adapter.BaseType, substitutions);
    }

    private static ResolvedAdapterExpose ResolveAdapterExpose(
        IReadOnlyList<TypeAdapterNode> adapters,
        TypeAdapterNode adapter,
        ExposeMethodNode expose,
        IReadOnlyList<string> receiverArguments)
    {
        var currentAdapter = adapter;
        var currentExpose = expose;
        var currentArguments = receiverArguments;
        var seen = new HashSet<string>(StringComparer.Ordinal);

        while (true)
        {
            var baseType = SubstituteAdapterBaseType(currentAdapter, currentArguments);
            var baseOwner = GetGenericBaseName(baseType) ?? baseType;
            var baseArguments = TryParseGenericUse(baseType, out _, out var parsedBaseArguments)
                ? parsedBaseArguments
                : [];
            var key = $"{currentAdapter.Name}.{currentExpose.ExposedName}";
            if (!seen.Add(key))
            {
                return new ResolvedAdapterExpose(baseType, baseOwner, currentExpose.SourceName, baseArguments);
            }

            var nextAdapter = adapters.FirstOrDefault(adapter => adapter.Name == baseOwner);
            var nextExpose = nextAdapter?.ExposedMethods.FirstOrDefault(expose =>
                expose.IsStatic == currentExpose.IsStatic
                && expose.ExposedName == currentExpose.SourceName);
            if (nextAdapter is null || nextExpose is null)
            {
                return new ResolvedAdapterExpose(baseType, baseOwner, currentExpose.SourceName, baseArguments);
            }

            currentAdapter = nextAdapter;
            currentExpose = nextExpose;
            currentArguments = baseArguments;
        }
    }

